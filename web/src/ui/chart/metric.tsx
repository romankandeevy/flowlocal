// Карточка-метрика: подпись моноширинным, число - крупно.
//
// Канон Korti (templates/app-shell, блок метрик): подпись 11px mono
// --text-muted, значение крупным весом. Такое сочетание - голос системы:
// «числа и результат вместо прилагательных» (guidelines/brand-voice).
//
// Число намеренно крупнее всего в карточке. Это единственное место, где
// человек видит не то, что программа умеет, а то, что она для него сделала.
// Поэтому оно и появляется живо: карточки всплывают лесенкой, а «живые» числа
// добегают до значения снизу вверх - не украшение, а «вот это про вас».
//
// Тени нет и быть не должно - см. довод в card.tsx: внутри движется число, и
// тень поднимала бы слой на композитор на каждом кадре счёта.

import { useEffect, useRef, useState } from "react";

import { Card } from "../card";
import { cn } from "../cn";
import { Icon, type IconName } from "../icons";

/**
 * Длительность токена в миллисекундах.
 *
 * Своим числом её писать нельзя: theme.css печатается из theme.py, и три
 * длительности системы обязаны меняться разом. Читаем с корня - там же, где
 * их объявил @theme.
 */
function durationMs(name: string, fallback: number): number {
  if (typeof document === "undefined") return fallback;
  const raw = getComputedStyle(document.documentElement)
    .getPropertyValue(name)
    .trim();
  const n = Number.parseFloat(raw);
  if (!Number.isFinite(n)) return fallback;
  // Сейчас генератор печатает миллисекунды («280ms»), но секунды («0.28s»)
  // тоже разбираем: смена формата в gen_theme.py не должна проходить молча
  // счётчиком, добегающим за четверть секунды вместо 280 мс.
  return raw.endsWith("ms") ? n : n * 1000;
}

/**
 * Ложь на первый отрисованный кадр, правда дальше.
 *
 * Этого хватает, чтобы CSS-переход поехал от начального состояния к конечному:
 * первый кадр элемент стоит «свёрнутым», со второго - на месте, между ними
 * переход. Так появление рисует браузер, а React только один раз щёлкает
 * состоянием.
 *
 * Два requestAnimationFrame, а не один. Первый вызывается ДО отрисовки первого
 * кадра, и переключение внутри него проходит в том же кадре - браузер видит
 * сразу конечное значение, переход не начинается вовсе, элемент просто
 * оказывается на месте.
 */
/**
 * Просил ли человек не двигать ничего лишнего.
 *
 * Системная настройка, и уважать её надо не из вежливости: движение на экране
 * у части людей вызывает тошноту и головокружение, и Windows даёт эту галочку
 * ровно за этим. Появление карточек и добегающее число - первое, что под неё
 * попадает.
 *
 * Нет matchMedia - считаем, что просил. Так ведёт себя jsdom в тестах, и это
 * заодно чинит их: без ответа от медиазапроса счётчики крутились кадрами уже
 * после проверки, и React сыпал предупреждениями «не обёрнуто в act». Лечить
 * это в тесте значило бы подпирать симптом - настоящая причина в том, что
 * анимация не спрашивала разрешения.
 */
function stillPlease(): boolean {
  if (typeof matchMedia !== "function") return true;
  return matchMedia("(prefers-reduced-motion: reduce)").matches;
}

export function useAfterPaint(): boolean {
  const [done, setDone] = useState(stillPlease);
  useEffect(() => {
    if (stillPlease()) return;
    let second = 0;
    const first = requestAnimationFrame(() => {
      second = requestAnimationFrame(() => setDone(true));
    });
    return () => {
      cancelAnimationFrame(first);
      cancelAnimationFrame(second);
    };
  }, []);
  return done;
}

/**
 * Число, добегающее до значения.
 *
 * Единственное движение в этих файлах не на CSS, и это не небрежность:
 * перехода для ТЕКСТА в CSS нет - интерполировать содержимое элемента браузер
 * не умеет. Правило «анимации только переходами» держится про геометрию и
 * краски; число приходится считать самим. Библиотека для этого не нужна -
 * кадр, доля, кривая, десять строк, - а длительность всё равно взята у токена.
 *
 * Считаем от ПОКАЗАННОГО значения, а не от нуля. Окно живёт в трее сутками, и
 * счётчик, отыгравший один раз при сборке окна, застревал навсегда: у нового
 * человека первое открытие давало «надиктовано 0», он диктовал, возвращался -
 * и видел рядом честное «сэкономлено 46 с» и ноль. Грабля записана в
 * Metric.qml, здесь она закрыта тем, что цель - зависимость эффекта.
 */
export function useCountUp(target: number | null): number {
  const [shown, setShown] = useState(0);
  const current = useRef(0);

  useEffect(() => {
    if (target === null) return;
    const from = current.current;
    const to = Math.max(0, target);
    if (stillPlease()) {
      // Число показываем сразу. Смысл метрики в самом числе, а не в том, как
      // оно доехало, - и без движения оно не теряется ни на грош.
      current.current = to;
      setShown(to);
      return;
    }
    const dur = durationMs("--dur-slow", 280);
    const started = performance.now();
    let id = 0;

    const tick = (now: number) => {
      const t = Math.min(1, (now - started) / dur);
      // Приближение токена --ease-out: быстро стартует, мягко садится.
      // Решать кубическую Безье ради счётчика незачем - на 280 мс разницы
      // между ней и кубикой не видно, а лишний десяток строк виден.
      const v = from + (to - from) * (1 - Math.pow(1 - t, 3));
      current.current = v;
      setShown(v);
      if (t < 1) id = requestAnimationFrame(tick);
    };

    id = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(id);
  }, [target]);

  return shown;
}

export interface MetricProps {
  /** Подпись над числом: «сэкономлено», «слов всего». */
  label: string;
  /** Готовая строка значения из Python: «46 с», «в 3,0 раза». */
  value?: string;
  /** Мелкая строка под числом: «слов за всё время». */
  hint?: string;
  /**
   * Значок у подписи. Пусто - подпись без значка, как на «Главной». На
   * «Статистике» он даёт метрике опознавалку с одного взгляда: время, слова,
   * серия - каждая со своим знаком.
   */
  icon?: IconName;
  /**
   * Чистое число - его показываем счётчиком вместо value. Форматированные
   * строки («46 с») так не оживить, а счётчик слов - можно, и он самый
   * убедительный из троих.
   */
  countTo?: number;
  /** Хвост к числу счётчика, если он нужен. */
  suffix?: string;
  /** Место в ряду - шаг лесенки появления. */
  order?: number;
  /**
   * Числа ещё нет, но будет. Не то же самое, что ноль.
   *
   * Скорость печати нельзя посчитать, пока человек не наговорил хотя бы
   * минуту, и раньше на её месте стоял прочерк - тем же кеглем и тем же
   * жирным начертанием, что и настоящие числа рядом. Читалось это как
   * сломанное значение: два числа и одно битое. Поэтому ожидающая метрика
   * говорит словами и вполголоса: обычный кегль, приглушённый цвет. Строй ряда
   * сохраняется, но карточка честно выглядит приглашением, а не поломкой.
   */
  pending?: boolean;
}

export function Metric({
  label,
  value = "",
  hint,
  icon,
  countTo,
  suffix = "",
  order = 0,
  pending = false,
}: MetricProps) {
  const shown = useAfterPaint();
  const counted = useCountUp(countTo === undefined ? null : countTo);
  const display =
    countTo === undefined ? value : `${Math.round(counted)}${suffix}`;

  return (
    // Появление живёт на обёртке, а не на самой карточке: у Card нет пропа
    // style, а шаг лесенки - число, которого Tailwind в классе не увидит.
    //
    // Прозрачность плюс лёгкий рост, а не подъём снизу. Подъём читается как
    // «элемент приехал откуда-то», то есть тянет за собой вопрос «откуда»;
    // рост из 98 % читается как «элемент проявился здесь» - у него нет ни
    // направления, ни источника, и спрашивать не о чем. На карточке, которая
    // просто есть на странице, верно второе.
    <div
      className={cn(
        "transition-[opacity,transform] duration-[var(--dur)] ease-out",
        shown ? "scale-100 opacity-100" : "scale-[0.98] opacity-0",
      )}
      style={{
        // Вторая карточка ждёт первую, третья - вторую. Шаг - половина
        // быстрого токена, а не своё число: задержка лесенки часть того же
        // движения, и когда токен изменят, шаг обязан поехать с ним.
        transitionDelay: `calc(var(--dur-fast) / 2 * ${order})`,
      }}
    >
      <Card shadow={false}>
        <div className="flex flex-col gap-2 p-4">
          {/* Значок того же приглушённого цвета, что и подпись, и на ступень
              крупнее её кегля - иначе штрих Lucide рядом с 11px мокнет в фон. */}
          <div className="flex items-center gap-1.5 text-text-muted">
            {icon ? <Icon name={icon} size={14} /> : null}
            <span className="font-mono text-2xs">{label}</span>
          </div>

          {pending ? (
            <p className="font-sans text-md leading-[1.3] text-text-secondary">
              {value}
            </p>
          ) : (
            <>
              {/* 20, а не канонические 32. В Korti 32 - кегль для ОДНОГО числа
                  на экране; у нас их три в ряд, и рядом заголовок страницы:
                  числа перебивали его, и страница читалась как три заголовка
                  подряд. Вес 700, не 800: Onest в ExtraBold слишком чёрный.
                  Трекинг в em, а не готовым числом, - при смене шкалы кеглей
                  готовое молча перестало бы быть -.03em.

                  Длинное значение здесь переносится, а не ужимается: у Qt был
                  HorizontalFit, в CSS такого нет вовсе. Ряд от этого не
                  кривится - три метрики стоят одной строкой сетки и тянутся
                  по самой высокой. */}
              <div className="font-sans text-xl font-bold tracking-[-0.03em] break-words tabular-nums text-text">
                {display}
              </div>
              {hint ? (
                <p className="font-sans text-2xs text-text-muted">{hint}</p>
              ) : null}
            </>
          )}
        </div>
      </Card>
    </div>
  );
}
