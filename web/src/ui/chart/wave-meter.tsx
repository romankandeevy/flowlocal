// Живая волна мастера первого запуска: столбики по громкости микрофона.
//
// Отдельным файлом, потому что мест два - экран микрофона и экран пробы, - а
// нарисованные по отдельности они разъедутся на первой же правке размеров.
//
// Уровни приходят пропсом от настоящего recorder приложения, а не от муляжа:
// мастер, который показывает нарисованную волну, проверяет сам себя, и
// «микрофон не тот» человек узнал бы уже после мастера. Сам компонент за
// данными не ходит и таймера не держит - тянуть их должен тот, кто знает, что
// экран сейчас виден: drain_levels отдаёт замеры ОДИН раз, и два экрана,
// тянущие разом, обкрадывали бы друг друга.
//
// Пилюля свою волну рисует сама (Overlay.qml, остаётся на Qt): у неё другой
// источник, другой размер и render-поток вместо вебвью.

import { cn } from "../cn";

/**
 * Замер громкости -> высота столбика, 0..1.
 *
 * Корень от отношения к порогу, а не сама громкость: линейная почти всё время
 * лежит у нуля, и волна на ней выглядит мёртвой - проверка микрофона тогда не
 * отвечает на свой единственный вопрос «меня слышно прямо сейчас».
 */
export function levelFromRms(rms: number): number {
  return Math.min(1, Math.pow(rms / 0.12, 0.65));
}

/**
 * Новое значение пик-метра: держится и спадает, как на пульте.
 *
 * По нему страница зажигает точку «слышу вас». Повторять каждый замер нельзя:
 * речь идёт волнами, между словами всегда тихо, и честная строка моргала бы
 * тридцать раз в секунду.
 */
export function decayPeak(peak: number, level: number): number {
  return Math.max(peak * 0.92, level);
}

export interface WaveMeterProps {
  /**
   * Уровни 0..1, свежий - последним. Короче bars - слева честно лежит ровная
   * линия, а не пустое место.
   */
  levels: number[];
  bars?: number;
  barWidth?: number;
  gap?: number;
  maxHeight?: number;
  className?: string;
}

export function WaveMeter({
  levels,
  bars = 40,
  barWidth = 5,
  gap = 3,
  maxHeight = 96,
  className,
}: WaveMeterProps) {
  return (
    // Волна - картинка звука, и рядом с ней страница ставит слово («слышу
    // вас»). Диктору читают слово, а сорок столбиков ему сказать нечего.
    <div
      aria-hidden="true"
      className={cn("flex items-center justify-center", className)}
      style={{ height: maxHeight, gap }}
    >
      {Array.from({ length: bars }, (_unused, i) => {
        // Свежий замер - справа: волна едет влево, как осциллограф.
        const level = levels[levels.length - bars + i] ?? 0;
        return (
          <div
            key={i}
            className="shrink-0 rounded-full bg-ink/85 transition-[height] ease-out"
            style={{
              width: barWidth,
              // Тишина - ниточка в три пикселя, а не пустота: иначе непонятно,
              // включён ли микрофон вообще.
              height: Math.max(3, level * maxHeight),
              // Столбик идёт за голосом, а не проигрывает переход: половина
              // быстрого токена. На целых --dur-fast полоска отстаёт от слова
              // настолько, что проверка микрофона перестаёт отвечать на свой
              // единственный вопрос.
              transitionDuration: "calc(var(--dur-fast) / 2)",
            }}
          />
        );
      })}
    </div>
  );
}
