// Пустая страница: знак, одна строка «что это» и одно действие «как начать».
//
// Вид не придуман заново. Он утверждён на «Статистике»: заголовок кеглем md,
// под ним сочетание клавиш плашками, под ними строка про то, что сделать.
// Переделывать было бы хуже, чем повторить: два разных пустых экрана в одной
// программе читаются как два разных приложения.
//
// Почему пустоту вообще надо закрывать. Список без записей и поле без слов
// выглядят одинаково с не загрузившимся списком и сломанным полем. Человек,
// который поставил программу пять минут назад, увидит именно поломку - у него
// ещё нет опыта, чтобы отличить «пусто» от «не работает».
//
// ---- Здесь нет ни одного обращения к мосту, и это условие, а не случайность ----
//
// EmptyState ставят разные страницы и диалоги, и опираться на то, что у
// хозяина есть подключение к Python, он не вправе. В QML это стоило дороже:
// одно упоминание `B` в привязке - и хозяин без него падал в ReferenceError,
// причём молча, потому что консоли под pythonw не существует. И сочетание, и
// действие приходят снаружи, а читает их тот, у кого мост есть.
import type { ReactNode } from "react";

import { Button } from "../button";
import { cn } from "../cn";
import { Icon, type IconName } from "../icons";

export function EmptyState({
  icon,
  title,
  hint,
  combo,
  actionLabel,
  onAction,
  alarm = false,
  className,
}: {
  /** Знак из набора. Пусто - без знака: заголовок останется первым. */
  icon?: IconName;
  /** «Что это»: одна строка. Название страницы не пересказываем - человек
   *  нажал её сам и уже знает, куда попал. */
  title?: string;
  /** «Как начать»: одна мысль. Здесь же говорим, что случится дальше. */
  hint?: ReactNode;
  /**
   * Сочетание клавиш плашками. Тип - готовый узел, а не строка с сочетанием:
   * плашки рисует KeyCaps, он живёт отдельно, и пустое состояние не должно
   * знать о нём ничего (ровно по той же причине, по какой не знает о мосте).
   * Страница передаёт сюда <KeyCaps combo={...} />, а не заставляет нас его
   * искать.
   */
  combo?: ReactNode;
  /** Кнопка под подсказкой. Пусто - кнопки нет. */
  actionLabel?: string;
  onAction?: () => void;
  /**
   * Начать нечем: сочетание не задано, микрофона нет. Подсказка краснеет.
   * Красный тут честный - человеку и правда нужно сначала сходить в другое
   * место, иначе страница не заработает никогда.
   */
  alarm?: boolean;
  className?: string;
}) {
  return (
    <div className={cn("flex flex-col items-start gap-3 p-6", className)}>
      {/* Знак в плашке, а не сам по себе. Голый штрих Lucide на пустой
          странице теряется: вокруг ничего нет, и глазу не за что зацепиться.
          Заливка и волосяная линия - те же, что у клавиш, своих красок здесь
          не заведено. */}
      {icon ? (
        <div
          className={cn(
            "flex size-11 items-center justify-center rounded-md",
            "border border-border bg-fill-subtle text-text-muted",
          )}
        >
          <Icon name={icon} size={22} />
        </div>
      ) : null}

      {/* Заголовком не делаем намеренно: это не раздел страницы, а замена
          её содержимого, и в списке заголовков диктора ему делать нечего -
          свой заголовок у страницы уже есть. */}
      {title ? (
        <p className="font-sans text-md font-semibold tracking-[-0.2px] text-text">
          {title}
        </p>
      ) : null}

      {combo}

      {hint ? (
        <p
          className={cn(
            "font-sans text-sm leading-[1.5]",
            alarm ? "text-danger" : "text-text-muted",
          )}
        >
          {hint}
        </p>
      ) : null}

      {actionLabel ? (
        <Button kind="primary" label={actionLabel} onClick={onAction} />
      ) : null}
    </div>
  );
}
