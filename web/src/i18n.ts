// Английский интерфейс страницы. Ключ перевода - сама русская строка.
//
// Так же, как в QML: там `L.t("Диктовка")`, здесь `t("Диктовка")`. Ключом
// служит русский текст, а не выдуманный идентификатор, и это осознанно -
// строку видно прямо в разметке, а не за именем вроде `settings.dictation.title`,
// про которое надо гадать. Не нашли перевода - показываем русское, то есть
// худший исход это «не перевелось», а не пустое место.
//
// **Словарь подтягивается, а не лежит в бандле.** 880 пар - около 20 КБ gzip,
// а бандл упирается в 120 КБ, отведённые планом. Русскому большинству словарь
// не нужен вовсе, поэтому файл берётся только при включённом английском - и
// берётся с 127.0.0.1, у нашего же сервера, то есть мгновенно.

let lang = "ru";
let dict: Record<string, string> = {};
const waiting: Array<() => void> = [];

/** Перевести. Русский - вернётся как есть, и это не ошибка. */
export function t(text: string): string {
  if (lang !== "en") return text;
  return dict[text] ?? text;
}

/** Текущий язык интерфейса. */
export function language(): string {
  return lang;
}

/**
 * Переключить язык. Английский тянет словарь один раз за сеанс.
 *
 * Возвращает обещание, чтобы вызывающий мог дождаться словаря и перерисовать
 * окно уже переведённым. Не дождался - страница просто останется русской до
 * следующей перерисовки, и это лучше, чем мигание половины строк.
 */
export async function setLanguage(next: string): Promise<void> {
  lang = next === "en" ? "en" : "ru";
  if (lang !== "en" || Object.keys(dict).length > 0) return;
  try {
    const res = await fetch("i18n.en.json");
    if (!res.ok) throw new Error(String(res.status));
    dict = (await res.json()) as Record<string, string>;
  } catch (e) {
    // Молча остаёмся на русском. Английский интерфейс - удобство, и ронять
    // из-за него окно нельзя: человек пришёл диктовать, а не читать подписи.
    console.warn("словарь не загрузился, остаёмся на русском", e);
  }
  waiting.splice(0).forEach((fn) => fn());
}

/** Подписаться на приход словаря - чтобы перерисовать окно. */
export function onDictReady(fn: () => void): void {
  if (lang !== "en" || Object.keys(dict).length > 0) fn();
  else waiting.push(fn);
}
