// Окно расшифровки: обёртка над Transcript, которая ходит в Python.
//
// Разделено так же, как picker: сам компонент проверяется без моста, обёртка
// знает про мост и больше ни про что.
import { saveText } from "../bridge/api";
import { Transcript } from "./transcript";

export function TranscriptView() {
  // Текст расшифровки Python пока не передаёт: окно открывается по готовности
  // разбора файла, и передача сделана в app.py прямым свойством QML-окна.
  // Мост так не умеет и уметь не должен - для этого понадобился бы пятый тип
  // сообщения «вот тебе данные». Проще, когда окно откроется, положить их
  // в конфиг под служебным ключом или добавить слот; решается в фазе 7,
  // вместе с остальной перепиской окон.
  const text = "";

  return (
    <Transcript
      text={text}
      // Диалог «куда сохранить» живёт в Python и жить будет: у страницы нет
      // доступа к диску, а «сохранить как» из браузера кладёт файл в загрузки,
      // а не туда, куда человек показал. Слот сам шлёт flashed - подпись у
      // кнопок подхватится без нашего участия.
      onSave={(value) => void saveText(value, "")}
    />
  );
}
