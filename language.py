# Буквы, которых в транслите английского не бывает. Мягкий и твёрдый знаки, «ы»
# и «щ» - это русская орфография как таковая: они помечают мягкость и звуки,
# которых в английском нет, и записывать ими английское слово незачем.
# Признак сильный и дешёвый: он спасает «отправь», «десять», «выключить» -
# слова с честным русским видом, но с окончанием, которого нет ни в одном
# списке хвостов.
_RU_LETTERS = frozenset("ъыьщ")

# Каркас английского в кириллической записи: артикли, местоимения, предлоги,
# вспомогательные глаголы. Это не словарь английского, а его служебная часть -
# то, что в английской фразе есть почти всегда, а в русской не бывает вовсе.
#
# Наличие такого слова - ПОЛОЖИТЕЛЬНАЯ улика, и нужна она там, где отсутствие
# русских признаков само по себе ничего не доказывает: список голых
# существительных («роутер, ноутбук, принтер») выглядит нерусским ровно так же,
# как транслит английского.
#
# Заимствований здесь нет намеренно. «Плиз», «сорри», «окей», «бай» давно
# живут в русской речи, и уликой английского быть не могут.
