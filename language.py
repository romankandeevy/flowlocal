# Часть 2. Переспрос: модель, которая пишет английский латиницей.

MODEL = "whisper-base"
QUANT = "int8"
SIZE_MB = 108              # столько заняло на диске после настоящей скачки
LANG = "en"

# Служебный идентификатор для `--download-model`: короткое слово вместо имени
# модели, чтобы вызов из мастера не зависел от того, какой моделью мы
# переспрашиваем сегодня.
DOWNLOAD_ID = "english"


def folder() -> str:
    """Папка модели - рядом с приложением, как у всех остальных.

    Не кэш HuggingFace: он лежит в C:/Users/<имя>, а имя у владельца
    кириллицей (HANDOFF, грабли onnx-asr).
    """
    from transcriber import model_dir

    return model_dir(MODEL, QUANT)


def size_mb() -> float:
    d = folder()
    if not os.path.isdir(d):
        return 0.0
    try:
        return sum(os.path.getsize(os.path.join(d, f))
                   for f in os.listdir(d)) / 1e6
    except OSError:
        return 0.0


def installed() -> bool:
    """Скачана ли модель целиком.

    Меряем объёмом, а не списком файлов, и по той же причине, что в
    `settings_qt.modelsInfo`: оборванная скачка оставляет папку с частью
    файлов, и она не должна выглядеть готовой моделью.
    """
    return size_mb() >= SIZE_MB * 0.9


def download() -> str:
    """Скачать модель. Пустая строка - получилось, иначе причина.

    Зовётся из отдельного ПРОЦЕССА (app.download_model_cli), а не из потока:
    сборка сессии onnxruntime идёт в C и держит GIL секундами, и окно на это
    время перестаёт отвечать. Ровно на этом владелец застрял в мастере первого
    запуска - «после появления полоски всё зависло».
    """
    import onnx_asr

    d = folder()
    # Папку НЕ создаём заранее, и это не мелочь. Для onnx-asr существующая
    # папка означает «работаем офлайн»: он не пойдёт качать и упадёт на
    # ненайденном файле. Пустая папка, созданная нами из вежливости, ломает
    # скачивание целиком (проверено - падает на первой же попытке).
    #
    # Неполную папку от оборванной скачки сносим по той же причине: докачать в
    # неё onnx-asr не сможет никогда.
    if os.path.isdir(d) and not installed():
        shutil.rmtree(d, ignore_errors=True)
    try:
        # load_model и качает, и собирает сессию. Собранную выбрасываем: нам
        # нужны файлы на диске. Зато так мы уверены, что скачано ровно то и в
        # том виде, что попросит настоящая загрузка.
        onnx_asr.load_model(MODEL, path=d, quantization=QUANT,
                            providers=["CPUExecutionProvider"])
        return "" if installed() else "скачалось не полностью"
    except Exception as e:  # noqa: BLE001 - из сети приходит что угодно
        return f"{type(e).__name__}: {e}"[:200]


class Recognizer:
    """Модель переспроса. Поднимается один раз и живёт в памяти."""

    def __init__(self, log=None) -> None:
        self.log = log or (lambda *_a: None)
        self.model = None

    def load(self) -> None:
        import onnx_asr
        from onnx_asr.utils import ModelFileNotFoundError

        d = folder()
        # providers жёстко: на DirectML whisper падает на первом же Conv, а
        # авто-перебор у нас общий с основной моделью и до CPU не дойдёт -
        # dml в списке доступных стоит раньше.
        kw = {"path": d, "quantization": QUANT,
              "providers": ["CPUExecutionProvider"]}
        try:
            self.model = onnx_asr.load_model(MODEL, **kw)
        except ModelFileNotFoundError:
            # Существующая папка для onnx-asr значит «работаем офлайн», и
            # докачивать он в неё не пойдёт. Неполную сносим и качаем заново -
            # тот же приём, что в transcriber._load_once.
            if not os.path.isdir(d):
                raise
            self.log(f"английская модель в {d} неполная - качаю заново")
            shutil.rmtree(d, ignore_errors=True)
            self.model = onnx_asr.load_model(MODEL, **kw)
        self._warmup()

    def _warmup(self) -> None:
        """Прогнать секунду тишины сразу после загрузки.

        Тем же приёмом, что в `transcriber._load`, и по той же причине: сессия
        собирается быстро, а кернелы инициализируются на ПЕРВОМ прогоне.
        Замерено: без прогрева первая фраза считается 2.98 с, вторая - 0.29 с,
        звук один и тот же.

        Секунд это никому не экономит, и врать тут нечего: те же 2.5 с просто
        переезжают из распознавания в загрузку. Смысл в том, ЧТО человек видит,
        пока ждёт. На загрузке пилюля пишет «английский» - ожидание объяснено.
        Без прогрева пилюля успевала переключиться на обычное распознавание и
        замирала на три секунды молча, а замершее без объяснения владелец уже
        называл зависанием - на полосе скачивания моделей.

        Ошибку глотаем: прогрев - удобство, а не условие работы.
        """
        import numpy as np

        from recorder import SAMPLE_RATE

        try:
            self.model.recognize(np.zeros(SAMPLE_RATE, dtype=np.float32),
                                 sample_rate=SAMPLE_RATE, language=LANG)
        except Exception as e:  # noqa: BLE001
            self.log(f"прогрев английской модели не вышел: {e}")

    @property
    def loaded(self) -> bool:
        return self.model is not None

    def unload(self) -> None:
        self.model = None

    def transcribe(self, audio) -> str:
        """Распознать запись как английскую.

        `language` обязателен. Без него whisper угадывает язык сам и на
        короткой фразе угадывает мимо; с ним - пишет ровно то, что просили.
        """
        from recorder import SAMPLE_RATE

        if self.model is None:
            raise RuntimeError("английская модель ещё не загружена")
        return str(self.model.recognize(audio, sample_rate=SAMPLE_RATE,
                                        language=LANG) or "").strip()


_shared: Recognizer | None = None


def shared(log=None) -> Recognizer:
    """Один экземпляр на приложение: 102 МБ не стоит держать в двух копиях."""
    global _shared

    if _shared is None:
        _shared = Recognizer(log)
    return _shared


def release() -> None:
    """Отпустить модель и вернуть память - зовётся вместе с выгрузкой основной."""
    if _shared is not None:
        _shared.unload()
