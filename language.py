"""Английская речь: как её узнать и чем переспросить.

Беда, ради которой это написано. GigaAM обучена на русском и латиницы не пишет
вовсе - ни одна из её версий. Услышав английскую фразу, она не молчит и не
ошибается наполовину: она добросовестно записывает английские звуки русскими
буквами. «Do you speak English» превращается в «дуис пик инглиш», и владелец
про это сказал коротко - «очень плохо выглядит».

Отменено замером, не рассуждением: `gigaam-multilingual-ctc` английский СЛЫШИТ
заметно лучше обычной («ду ю спик энглиш» против «ту ю спик инглиш»), но алфавит
у неё тот же кириллический. Многоязычность там про языки, а не про письмо, и
беды она не решает.

Отсюда устройство в два шага:

1. **Определитель по тексту.** Признак английского лежит прямо в том, что уже
   написала GigaAM, - второй модели ДЛЯ ПРОВЕРКИ не нужно. Русская фраза от трёх
   слов почти всегда содержит служебное слово («в», «не», «что») или слово с
   русским окончанием, а транслит английского - нет.

2. **Переспрос.** Только когда определитель сказал «английский», ту же запись
   прогоняем через whisper-base с `language="en"`. Он пишет латиницей и со
   знаками: «Do you speak English?», «Please send me the report by Friday.»
   Для русского накладных расходов ровно ноль: одна проверка текста.

Чем это рискует и почему порог сдвинут в сторону молчания. Ложное срабатывание
дороже пропуска: whisper с `language="en"` русскую речь не распознаёт, а
ПЕРЕВОДИТ - «Стратегическая сессия начинается в пятницу» вышло у него как «The
strategic session begins at 5.10». То есть на ошибке определителя человек
получит в документ английский вместо русского. Пропустили английский - он
увидит кракозябры и повторит фразу.

Две грабли, обе замерены и обе обязательны:

- **Только CPUExecutionProvider.** На DirectML whisper падает («Conv node ...
  DmlExecutionProvider»), а у владельца в конфиге стоит именно видеокарта. Это
  не теория, это его машина.
- **Только `language="en"`.** Без языка whisper угадывает сам и на короткой
  фразе угадывает мимо.

Квантизация int8, а не fp32, и это тоже замер: 108 МБ против 399 и 0.24-0.26 с
против 0.38-0.40 на фразу. Текст при этом ДОСЛОВНО одинаковый на всех пробных
фразах. Сжатая вчетверо меньше и при этом быстрее - выбирать не из чего.

Замер определителя. Тринадцать настоящих выходов GigaAM с английской речи
(семь снял координатор, шесть - голосом Zira здесь) против ста сорока русских:
вся история диктовок владельца плюс нарочно трудные фразы с заимствованиями.
Ловится 11 из 13, ложных срабатываний ноль. Пропускаются оба варианта одной и
той же фразы («лера скежила миринг ту марал монинг» - это «Let us schedule a
meeting tomorrow morning»): модель разобрала её так, что она читается русской,
и никакой разбор текста тут не поможет.

Известный предел, оставленный намеренно. Фраза из одних заимствований без
единого русского слова («оффер, релокейт, стартап») будет принята за
английскую. Закрыть это можно только требованием английского служебного слова
всегда - а оно стоит трёх настоящих английских фраз из тринадцати, включая
«дуис пик инглиш». Три слова подряд, ни одного русского и ни одного глагола -
это не то, как говорят люди, которым программу отдают.
"""

