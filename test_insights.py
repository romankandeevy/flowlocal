"""Разбор своей речи: что считаем и когда честнее промолчать.

Половина проверок - про молчание. Сказать человеку «ваше любимое слово -
«который»» по десяти диктовкам значит соврать с умным видом, и цена такой
ошибки выше, чем у пустого экрана.

Запуск: python test_insights.py
"""

import sys

import insights as I

ok = True


def check(cond: bool, text: str) -> None:
    global ok
    ok = ok and bool(cond)
    print(("  ок   " if cond else "ПРОВАЛ ") + text)


def row(text, app="telegram.exe", ts="2026-07-18 14:30:00", raw=None):
    d = {"ts": ts, "sec": 5.0, "text": text, "app": app}
    if raw:
        d["raw"] = raw
    return d


def main() -> int:
    # --- молчим, пока данных мало ---
    s = I.summary([row("привет как дела")])
    check(s["enough"] is False, "на одной диктовке ничего не утверждаем")
    check(s["top_word"] == "" and s["phrase"] == "",
          "любимое слово и присказка молчат, а не показывают случайное")
    check(s["need_rows"] > 0 and s["need_words"] > 0,
          "видно, сколько ещё надо накопить")

    # --- любимое слово ---
    много = [row("стратегическая сессия начинается в пятницу")] * 4
    s = I.top_word(много)
    check(s[0] in ("стратегическая", "сессия", "начинается", "пятницу"),
          f"любимое слово найдено: {s[0]!r} ({s[1]} раз)")
    check(I.top_word([row("и в на не что")])[0] == "",
          "ЛОВУШКА: из одних служебных слов «любимое» не выбирается")
    check(I.top_word([row("дом кот")])[0] == "",
          "ЛОВУШКА: слово, сказанное раз, - не любимое")

    # --- присказка ---
    p = I.catch_phrase([row("ну как бы это самое дальше текст")] * 3)
    check(p[1] >= 3, f"присказка найдена: {p[0]!r} ({p[1]} раз)")
    check(I.catch_phrase([row("дом кот стол")] * 2)[0] == "",
          "ЛОВУШКА: дважды - ещё не присказка")

    # --- исправления ---
    n, worst = I.corrections([
        row("Соколов подтвердил", raw="сокалов подтвердил"),
        row("Соколов приедет", raw="сокалов приедет"),
        row("без разницы"),          # без raw - пропускаем
    ])
    check(n == 2 and worst == "сокалов",
          f"чаще всего путается «{worst}», исправлено {n}")
    check(I.corrections([row("текст")])[0] == 0,
          "ЛОВУШКА: записи без «услышано до чистки» не считаются")

    # --- часы ---
    h, n = I.peak_hour([row("а", ts="2026-07-18 09:00:00"),
                        row("б", ts="2026-07-18 09:40:00"),
                        row("в", ts="2026-07-18 21:00:00")])
    check(h == 9 and n == 2, f"час пик найден: {h}:00, {n} диктовки")
    check(I.peak_hour([{"text": "нет времени"}])[0] == -1,
          "ЛОВУШКА: без метки времени час не выдумывается")

    # --- куда диктуют ---
    w = I.where([row("а", app="outlook.exe"), row("б", app="outlook.exe"),
                 row("в", app="telegram.exe"), row("г", app="неизвестно.exe")])
    kinds = {x["kind"]: x["share"] for x in w}
    check(kinds.get("почта") == 50, f"почта - половина: {kinds}")
    # Раньше здесь проверялось, что незнакомая программа уходит в «остальное».
    # Правило отменено разбором вслух: у владельца в «остальное» падала
    # половина диктовок (Claude, Notion, редакторы), и раздел стал бесполезен.
    # Теперь незнакомая программа называет себя сама - по имени exe, без
    # догадок о том, чем в ней занимаются.
    check("Неизвестно" in kinds,
          f"незнакомая программа называется своим именем, а не «остальное»: {kinds}")
    check("остальное" not in kinds,
          "безымянной кучи больше нет")
    check(I.where([{"text": "без app"}]) == [],
          "ЛОВУШКА: у старых записей нет программы - раздел пуст, а не врёт")

    # --- роль ---
    длинные = [row(" ".join(["слово"] * 30), app="outlook.exe",
                   ts="2026-07-18 09:00:00") for _ in range(6)]
    role = I.persona(длинные, I.where(длинные))
    check("утрам" in role and "письма" in role,
          f"роль собрана из посчитанного: {role!r}")
    check("длинными" in role, "манера речи - по средней длине диктовки")
    check(I.persona([row("а")], I.where([row("а")])) == "",
          "ЛОВУШКА: на одной диктовке роль не выдумывается")
    check(I.persona([], []) == "", "ЛОВУШКА: пустая история не даёт роли")

    # --- ничего не падает на мусоре ---
    for плохое in ([{}], [{"text": None}], [{"text": ""}], []):
        I.summary(плохое)
    check(True, "битые и пустые записи не роняют разбор")

    print("\nИТОГ:", "всё сходится" if ok else "ЕСТЬ ПРОВАЛЫ")
    return 0 if ok else 1


if __name__ == "__main__":
    sys.exit(main())
