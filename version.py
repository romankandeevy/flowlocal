"""Версия приложения - один источник правды.

Отдельным модулем, а не в pyproject.toml: в собранном .exe никакого
pyproject рядом нет, а версию надо знать и обновлятору, и окну «О программе».
Отсюда же её берут tools/build.py (для установщика) и tools/release.py.

Правило: поднять здесь -> собрать -> tools/release.py. Больше нигде цифру
руками не писать, иначе установщик, приложение и релиз разъедутся, и
обновлятор начнёт предлагать «обновиться» на то, что уже стоит.

Схема - два числа, `МАЖОР.НОМЕР`. Мажор равен 1 и меняется только по прямому
решению владельца за эпохальное (macOS, полная переработка). Номер растёт на
единицу за выпуск, включая хотфиксы. Третьих и четвёртых чисел нет.
Подробно и с обоснованием - в шапке CHANGELOG.md.

Сравнение версий терпит любую длину: `updater._parse` берёт до четырёх чисел,
и (1, 0) > (0, 21, 0) считается правильно само собой - переход с 0.21.0 на 1.0
обновлятор видит как обычное обновление. Старые теги 0.x тоже разбираются, и
это не мёртвый код: у людей на руках стоят версии со старой схемой.

npm - единственное место, где три числа обязательны (реестр не примет «1.0»).
Туда tools/release.py дописывает ноль сам: 1.0 -> 1.0.0. Наружу это не
показывается.
"""

