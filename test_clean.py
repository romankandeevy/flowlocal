def run_commands_off() -> int:
    """Выключенные голосовые команды выключают и дату со временем.

    Отдельной проверкой, потому что здесь проверяется решение по конфигу, а не
    текст. Человек, снявший галочку, просил не трогать его слова - значит и
    «сегодняшняя дата» обязана остаться словами.
    """
    text = "Встреча сегодняшняя дата в десять."
    got, _ = extract_commands(text, {"voice_commands": False}, now=NOW)
    if got != text:
        print("  ПРОВАЛ [выключенные команды не должны подставлять дату]")
        print(f"    вышло: {got!r}")
        return 1
    print("голосовые команды выключены: 1/1")
    return 0


def run_gate() -> int:
    """Гейт по модели: правила работают только там, где модель знаков не ставит.

    Отдельной функцией, а не строкой в таблице: проверяем не текст, а решение
    конвейера, и для этого нужен конфиг.

    Зачем это вообще. У basic_punctuation гейт был по заглавным буквам, и при
    замене на punct_rules он едва не потерялся. Без него правила лезли бы
    поверх e2e-модели: она расставляет знаки сама и местами осознанно НЕ ставит
    запятую там, где правило её поставит. Вышли бы дубли и спор с моделью.

    Случай не выдуманный: у всех, кто обновился со старой версии, в конфиге
    лежит именно gigaam-v3-e2e-rnnt.
    """
    import cleaner
    import config as C

    сырое = "ну вот короче я думаю что надо сделать"
    bad = 0

    def проверь(model, ждём_знаки, why):
        nonlocal bad
        cfg = dict(C.DEFAULTS)
        cfg["llm"] = {"enabled": False}
        cfg["model"] = model
        got = cleaner.clean(сырое, cfg, lambda _m: None)
        тронули = got != сырое
        if тронули != ждём_знаки:
            bad += 1
            print(f"  ПРОВАЛ [{why}]")
            print(f"    модель: {model}")
            print(f"    вышло:  {got!r}")

    проверь("gigaam-v2-ctc", True, "модель без знаков - правила работают")
    проверь("gigaam-v2-rnnt", True, "RNN-T тоже без знаков")
    проверь("gigaam-v3-e2e-rnnt", False,
            "e2e ставит знаки сама - правила молчат")
    проверь("gigaam-v3-e2e-ctc", False, "второй e2e-вариант тоже")
    проверь("alphacep/vosk-model-ru", True, "модель не из каталога, без e2e")

    # Приёмочный критерий из задания, дословно.
    cfg = dict(C.DEFAULTS)
    cfg["llm"] = {"enabled": False}
    cfg["model"] = "gigaam-v2-ctc"
    got = cleaner.clean(сырое, cfg, lambda _m: None)
    if got != "Я думаю, что надо сделать.":
        bad += 1
        print("  ПРОВАЛ [приёмочный критерий]")
        print(f"    вышло: {got!r}")

    print(f"гейт по модели: {6 - bad}/6")
    return bad


def run(name, cases, fn) -> int:
    bad = 0
    for text, want, why in cases:
        got = fn(text)
        if got != want:
            bad += 1
            print(f"  ПРОВАЛ [{why}]")
            print(f"    было:  {text!r}")
            print(f"    ждали: {want!r}")
            print(f"    вышло: {got!r}")
    print(f"{name}: {len(cases) - bad}/{len(cases)}")
    return bad


def main() -> None:
    bad = run("паразиты", FILLERS, _strip_fillers)
    bad += run("запятые по правилам", PUNCT, punct_rules.apply)
    bad += run_gate()
    bad += run("словарь", DICTIONARY, lambda t: _apply_dictionary(t, DICT_WORDS))
    bad += run("переменные в подстановках", SNIPPETS,
               lambda t: _apply_snippets(t, SNIPPET_RULES, now=NOW))
    bad += run("дата и время голосом", VOICE_VARS,
               lambda t: extract_commands(t, {"voice_commands": True},
                                          now=NOW)[0])
    bad += run_commands_off()
    print("\nОК" if not bad else f"\nПРОВАЛЕНО: {bad}")
    sys.exit(1 if bad else 0)


if __name__ == "__main__":
    main()
