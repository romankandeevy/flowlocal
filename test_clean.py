    ("Эм, сессия начинается в пятницу в 10:00 утра. ммм, вот.",
     "Сессия начинается в пятницу в 10:00 утра.",
     "паразит в конце: осиротевшая точка не должна удвоить настоящую"),

    # --- ЛОВУШКИ: тронуть = потерять текст ---
    ("Вот дом, в котором я живу.", "Вот дом, в котором я живу.",
     "ЛОВУШКА: «вот» без запятой - обычное слово"),
    ("Короче путь через двор.", "Короче путь через двор.",
     "ЛОВУШКА: «короче» - сравнительная степень"),
    ("В общем виде это работает.", "В общем виде это работает.",
     "ЛОВУШКА: «в общем» - часть оборота"),
