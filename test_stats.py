"""Статистика: серия дней, экономия времени, неделя, рекорд и «когда это было».

Считалка простая, а врать может дорого: «сэкономлено» - первое число на
Главной, и завышенное оно превращает честный замер в рекламу. Отсюда ловушки:
медленная речь (экономии нет, но и минуса быть не должно), пустая история
(деление на ноль), дыра в серии.

Подпись времени (`ago`) проверяется здесь же, и тоже ловушками: граница суток
важнее арифметики - «вчера в 23:00» отстоит на два часа, а называется вчера.

Недельный график и рекорд - оттуда же: столбик «сегодня» подсвечен акцентом, и
подсветить он обязан настоящий сегодняшний день, а не последний день с
диктовкой. Ловушка ровно на это (молчали три дня), плюс ничья в рекорде.

Календарь серии (клетки «как на гитхабе») ловится тем же способом: сетка обязана
кончаться воскресеньем, дни после сегодняшнего - оставаться пустыми, а
насыщенность считаться от лучшего дня окна, а не от круглого числа.

Файлов не трогает, микрофон не нужен: чистая арифметика на синтетике.

    python test_stats.py
"""

from datetime import date, datetime

import stats

TODAY = date(2026, 7, 17)
W10 = " ".join(["слово"] * 10)

# «Сейчас» для подписей времени. Прибито намеренно: подпись обязана зависеть
# от данных, а не от того, в какой день запускают тест.
NOW = datetime(2026, 7, 17, 14, 0, 0)


def row(ts: str, sec: float, text: str = W10) -> dict:
    return {"ts": ts, "sec": sec, "text": text}


def _streak_cases() -> list[tuple[str, list[dict], int]]:
    return [
        ("сегодня и вчера",
         [row("2026-07-17 10:00:00", 5), row("2026-07-16 10:00:00", 5)], 2),
        # Сегодня человек мог просто ещё не сесть за письма - серия жива.
        ("сегодня не успел, вчера и позавчера",
         [row("2026-07-16 10:00:00", 5), row("2026-07-15 10:00:00", 5)], 2),
        # А вот оборвавшаяся неделю назад серия - уже не серия.
        ("оборвалась неделю назад",
         [row("2026-07-10 10:00:00", 5), row("2026-07-09 10:00:00", 5)], 0),
        ("дыра посередине - считается только хвост",
         [row("2026-07-17 10:00:00", 5), row("2026-07-15 10:00:00", 5)], 1),
        ("несколько диктовок за день - день один",
         [row("2026-07-17 10:00:00", 5), row("2026-07-17 18:00:00", 5)], 1),
        ("пустая история", [], 0),
    ]


def _ago_cases() -> list[tuple[str, str]]:
    """(отметка времени, как это должно читаться) при NOW = 17 июля, 14:00."""
    return [
        ("2026-07-17 13:59:30", "только что"),
        # Минута с небольшим - ещё «только что»: человек не считает секунды.
        ("2026-07-17 13:58:40", "только что"),
        ("2026-07-17 13:58:00", "2 минуты назад"),
        ("2026-07-17 13:55:00", "5 минут назад"),
        ("2026-07-17 13:59:00", "только что"),
        ("2026-07-17 13:00:00", "1 час назад"),
        ("2026-07-17 09:00:00", "5 часов назад"),
        # Ловушка на границу суток: разница два часа, а день другой.
        ("2026-07-16 23:00:00", "вчера"),
        ("2026-07-15 10:00:00", "2 дня назад"),
        # Неделя и дальше - дата словами: «34 дня назад» никто не переводит
        # в число в уме.
        ("2026-07-01 10:00:00", "1 июля"),
        ("2025-12-31 10:00:00", "31 декабря 2025"),
        # Ловушка: запись из будущего (перевели часы) - не «-5 минут назад».
        ("2026-07-17 14:30:00", "только что"),
        # Ловушка: битая строка. Пустая подпись лучше «Invalid Date».
        ("не время вовсе", ""),
        ("", ""),
    ]


def _week_cases() -> list[tuple[str, list[dict], list, str]]:
    """(имя, история, ожидаемые слова по семи дням, подпись сегодняшнего дня).

    TODAY = 17 июля 2026, это пятница. Значит окно недели - с субботы 11-го по
    пятницу 17-го, а подписи идут «сб вс пн вт ср чт пт».
    """
    return [
        ("сегодня и вчера",
         [row("2026-07-17 10:00:00", 5), row("2026-07-16 10:00:00", 5)],
         [0, 0, 0, 0, 0, 10, 10], "пт"),
        # Ловушка, ради которой week() и заведён отдельно от by_day: три дня
        # молчания. by_day считает «сегодня» последним днём истории и подсветил
        # бы вторник; неделя обязана кончаться пятницей и показать три нуля.
        ("молчали три дня",
         [row("2026-07-14 10:00:00", 5)],
         [0, 0, 0, 10, 0, 0, 0], "пт"),
        # Ловушка: старьё за окном недели не должно просочиться в столбики.
        ("история старше недели",
         [row("2026-07-01 10:00:00", 5), row("2026-07-17 10:00:00", 5)],
         [0, 0, 0, 0, 0, 0, 10], "пт"),
        # Ловушка: несколько диктовок за день складываются в один столбик.
        ("две диктовки за день",
         [row("2026-07-17 10:00:00", 5), row("2026-07-17 18:00:00", 5)],
         [0, 0, 0, 0, 0, 0, 20], "пт"),
        # Пустая история - всё равно семь дней с нулями, а не пустой список:
        # график рисует ровно то, что ему дали.
        ("пустая история", [], [0] * 7, "пт"),
    ]


def _calendar_checks() -> list[tuple[str, object, object]]:
    """(имя, что получили, что ждали) для сетки календаря серии.

    TODAY = пятница 17 июля 2026. Сетка кончается воскресеньем ЭТОЙ недели
    (19 июля), а не сегодняшним днём: столбец - это неделя целиком, и обрубать
    его посередине нельзя, иначе пятница встала бы в строку воскресенья.
    """
    out = []
    weeks = 4
    cal = stats.calendar([row("2026-07-17 10:00:00", 5),
                          row("2026-07-15 10:00:00", 5, W10 + " " + W10)],
                         today=TODAY, weeks=weeks)
    days = cal["days"]
    by_date = {d["date"]: d for d in days}

    out.append(("клеток ровно недели по семь", len(days), weeks * 7))
    # Сетка начинается понедельником и кончается воскресеньем - иначе строки
    # разъедутся с подписями «пн»…«вс».
    out.append(("первый день - понедельник", days[0]["date"], "2026-06-22"))
    out.append(("последний день - воскресенье", days[-1]["date"], "2026-07-19"))
    # Ловушка: суббота и воскресенье ещё не наступили. Закрашенное завтра
    # означало бы, что мы знаем про завтра.
    out.append(("завтра помечено будущим", by_date["2026-07-18"]["future"], True))
    out.append(("сегодня не будущее", by_date["2026-07-17"]["future"], False))
    out.append(("сегодня помечено", by_date["2026-07-17"]["today"], True))

    # Слова легли в свои дни, а не сползли на соседние.
    out.append(("слова в своём дне", by_date["2026-07-15"]["words"], 20))
    out.append(("день молчания - ноль", by_date["2026-07-16"]["words"], 0))
    # Насыщенность - доля от лучшего дня окна: 20 слов это пик (4), 10 - ровно
    # половина (2), ноль - молчали (0).
    out.append(("пик - полная заливка", by_date["2026-07-15"]["level"], 4))
    out.append(("половина пика - средняя", by_date["2026-07-17"]["level"], 2))
    out.append(("молчали - нулевая", by_date["2026-07-16"]["level"], 0))

    # Ловушка: пустая история. Сетка всё равно полная, но ни одной закрашенной
    # клетки - и деления на ноль внутри нет.
    empty = stats.calendar([], today=TODAY, weeks=weeks)
    out.append(("пусто - сетка на месте", len(empty["days"]), weeks * 7))
    out.append(("пусто - ни одной заливки",
                max(d["level"] for d in empty["days"]), 0))

    # Ловушка: битые строки не считаются словами и не роняют счёт.
    junk = stats.calendar([{"ts": "кривая", "sec": None, "text": 5}],
                          today=TODAY, weeks=weeks)
    out.append(("мусор не красит клетки",
                max(d["level"] for d in junk["days"]), 0))

    # Ловушка: диктовка старше окна не должна просочиться в сетку.
    old = stats.calendar([row("2025-01-01 10:00:00", 5)], today=TODAY, weeks=weeks)
    out.append(("старьё за окном не в счёт",
                max(d["words"] for d in old["days"]), 0))

    # Подписи месяцев - над столбцом, с которого месяц начался. Окно
    # 22 июня - 19 июля: июнь начинает нулевой столбец, июль - третий по счёту
    # понедельник, то есть столбец 2. Вторая июньская неделя своей подписи не
    # получает - месяц тот же.
    #
    # Ловушка на тесноту: обрезанный июнь занимает всего два столбца, и его
    # подпись налезла бы на июльскую. Остаётся одна - «июл».
    out.append(("подписи месяцев", [(m["label"], m["col"]) for m in cal["months"]],
                [("июл", 2)]))
    # А на полном окне подписи стоят все: полгода - это шесть-семь месяцев, и
    # выкидывать из них ничего не надо.
    half = stats.calendar([], today=TODAY)
    out.append(("на полном окне месяцы не теряются",
                [m["label"] for m in half["months"]],
                ["фев", "мар", "апр", "май", "июн", "июл"]))

    # Детерминированность: тот же ответ на тех же данных в любой день.
    one = [row("2026-07-17 10:00:00", 5)]
    later = stats.calendar(one, today=date(2027, 1, 1), weeks=weeks)
    out.append(("через год сегодня уже не то",
                max(d["level"] for d in later["days"]), 0))
    return out


def _best_cases() -> list[tuple[str, list[dict], object]]:
    return [
        ("один день",
         [row("2026-07-17 10:00:00", 5)], ("2026-07-17", 10)),
        ("день с двумя диктовками обгоняет день с одной",
         [row("2026-07-15 10:00:00", 5), row("2026-07-15 11:00:00", 5),
          row("2026-07-17 10:00:00", 5)], ("2026-07-15", 20)),
        # Ловушка на ничью: одинаковые дни - берём поздний, иначе ответ зависел
        # бы от порядка строк в файле.
        ("ничья - поздний день",
         [row("2026-07-15 10:00:00", 5), row("2026-07-17 10:00:00", 5)],
         ("2026-07-17", 10)),
        # Ловушка: рекорд ищется по всей истории, а не за последнюю неделю.
        ("рекорд годовой давности не стирается",
         [row("2025-03-02 10:00:00", 5, " ".join(["слово"] * 99)),
          row("2026-07-17 10:00:00", 5)], ("2025-03-02", 99)),
        ("пустая история", [], None),
        # Ловушка: история из одних битых строк - это тоже пусто.
        ("только мусор", [{"ts": "кривая", "sec": None, "text": 5}], None),
    ]


def main() -> None:
    bad = 0
    streak_bad = 0
    ago_bad = 0
    week_bad = 0
    best_bad = 0
    cal_bad = 0

    for ts, want in _ago_cases():
        got = stats.ago(ts, now=NOW)
        if got != want:
            ago_bad += 1
            print(f"ПЛОХО время: {ts!r} - ждали {want!r}, получили {got!r}")
    bad += ago_bad

    for name, rows, want in _streak_cases():
        got = stats.summary(rows, today=TODAY)["streak_days"]
        if got != want:
            streak_bad += 1
            print(f"ПЛОХО серия: {name} - ждали {want}, получили {got}")
    bad += streak_bad

    # Экономия считается по замеру, а не по средней скорости речи из учебника:
    # 20 слов набирать руками 30 с (40 сл/мин), наговорены за 10 с -> 20 с.
    s = stats.summary([row("2026-07-17 10:00:00", 10, " ".join(["слово"] * 20))],
                      today=TODAY)
    if abs(s["saved_sec"] - 20.0) > 0.01:
        bad += 1
        print(f"ПЛОХО экономия: ждали 20 с, получили {s['saved_sec']:.1f} с")
    if abs(s["speech_wpm"] - 120) > 0.01:
        bad += 1
        print(f"ПЛОХО скорость: ждали 120 сл/мин, получили {s['speech_wpm']:.1f}")

    # Ловушка: говорил медленнее, чем печатал бы. Экономии нет - но и
    # отрицательной она быть не может, иначе на Главной появится «-45 с».
    slow = stats.summary([row("2026-07-17 10:00:00", 60)], today=TODAY)
    if slow["saved_sec"] != 0.0:
        bad += 1
        print(f"ПЛОХО медленная речь: ждали 0, получили {slow['saved_sec']:.1f}")

    # Ловушка: пустая история - ноль записей, ноль секунд, деления на ноль нет.
    empty = stats.summary([], today=TODAY)
    if empty["speech_wpm"] != 0 or empty["saved_sec"] != 0:
        bad += 1
        print(f"ПЛОХО пусто: {empty['speech_wpm']}, {empty['saved_sec']}")

    # Детерминированность: те же данные обязаны давать тот же ответ - иначе
    # это не проверить (тот же приём, что в by_day).
    one = [row("2026-07-17 10:00:00", 5)]
    then = stats.summary(one, today=TODAY)["streak_days"]
    later = stats.summary(one, today=date(2027, 1, 1))["streak_days"]
    if (then, later) != (1, 0):
        bad += 1
        print(f"ПЛОХО детерминированность: {then}, {later}")

    for name, rows, want, want_today in _week_cases():
        days = stats.week(rows, today=TODAY)
        got = [d["words"] for d in days]
        # Сегодняшний столбик ровно один, и он последний: иначе акцент на
        # графике встанет не туда или сразу на два дня.
        marked = [d["label"] for d in days if d["today"]]
        if got != want or marked != [want_today]:
            week_bad += 1
            print(f"ПЛОХО неделя: {name} - ждали {want}/{want_today}, "
                  f"получили {got}/{marked}")
    bad += week_bad

    # Подписи дней считаются от календаря, а не от порядка в истории.
    labels = [d["label"] for d in stats.week([], today=TODAY)]
    if labels != ["сб", "вс", "пн", "вт", "ср", "чт", "пт"]:
        bad += 1
        print(f"ПЛОХО подписи дней: {labels}")
    # Дата словами - для подсказки под курсором и строки рекорда.
    if stats.week([], today=TODAY)[-1]["human"] != "17 июля":
        bad += 1
        print("ПЛОХО дата словами: " + stats.week([], today=TODAY)[-1]["human"])

    for name, rows, want in _best_cases():
        got = stats.best_day(rows)
        pair = None if got is None else (got["date"], got["words"])
        if pair != want:
            best_bad += 1
            print(f"ПЛОХО рекорд: {name} - ждали {want}, получили {pair}")
    bad += best_bad

    for name, got, want in _calendar_checks():
        if got != want:
            cal_bad += 1
            print(f"ПЛОХО календарь: {name} - ждали {want}, получили {got}")
    bad += cal_bad

    n = len(_streak_cases())
    print(f"серия: {n - streak_bad}/{n}")
    a = len(_ago_cases())
    print(f"когда это было: {a - ago_bad}/{a}")
    w = len(_week_cases())
    print(f"неделя: {w - week_bad}/{w}")
    b = len(_best_cases())
    print(f"рекорд: {b - best_bad}/{b}")
    c = len(_calendar_checks())
    print(f"календарь: {c - cal_bad}/{c}")
    print("экономия, скорость, ловушки: "
          + ("ок" if bad == streak_bad + ago_bad + week_bad + best_bad + cal_bad
             else "провал"))
    print("\nОК" if bad == 0 else f"\nПРОВАЛОВ: {bad}")
    raise SystemExit(1 if bad else 0)


if __name__ == "__main__":
    main()
