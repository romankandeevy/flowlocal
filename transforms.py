MAX_CUSTOM = 12          # больше не влезет в список, который выбирают с клавиатуры


def _clean_custom(raw) -> list[dict]:
    """Свои преобразования из конфига. Битые записи молча пропускаем.

    Конфиг человек правит руками, и одна кривая строка не должна лишать его
    всего списка - тем более что список этот открывается по хоткею посреди
    работы, а не в спокойной обстановке.
    """
    out: list[dict] = []
    if not isinstance(raw, list):
        return out
    for i, item in enumerate(raw):
        if not isinstance(item, dict):
            continue
        title = str(item.get("title") or "").strip()
        instruction = str(item.get("instruction") or "").strip()
        if not title or not instruction:
            continue
        out.append({
            "id": str(item.get("id") or f"custom{i}"),
            "title": title[:40],
            "hint": str(item.get("hint") or "").strip()[:60],
            "instruction": instruction,
            "custom": True,
        })
        if len(out) >= MAX_CUSTOM:
            break
    return out


def all_for(cfg: dict) -> list[dict]:
    """Что показать в списке: готовые (кроме спрятанных) плюс свои.

    Спрятать готовое можно, удалить - нельзя: список задан кодом, и удалять
    из него нечего. Спрятанные лежат в конфиге по id, чтобы человек, который
    никогда не переводит на английский, не видел этот пункт вечно.
    """
    hidden = set(cfg.get("transforms_hidden") or [])
    out = [dict(p) for p in PRESETS if p["id"] not in hidden]
    out.extend(_clean_custom(cfg.get("transforms")))
    return out


def find(cfg: dict, tid: str) -> dict | None:
    for t in all_for(cfg):
        if t["id"] == tid:
            return t
    return None
