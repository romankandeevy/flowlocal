def merge_into(snippets) -> dict:
    """Добавить термины к подстановкам человека, не трогая его собственные.

    Подстановки хранятся словарём «сказать -> вставить» (см. _apply_snippets),
    поэтому и здесь словарь. Его записи главнее: если он уже завёл «апи» на
    что-то своё, мы молчим.
    """
    out = dict(snippets or {})
    for say, put in TERMS.items():
        if say not in {k.lower() for k in out}:
            out[say] = put
    return out


def strip_from(snippets) -> dict:
    """Убрать наши термины, оставив собственные записи человека.

    Сверяем и ключ, и значение: если человек переопределил «апи» на своё,
    запись наша только по названию, и удалять её нельзя.
    """
    return {k: v for k, v in (snippets or {}).items()
            if not (k.lower() in TERMS and v == TERMS[k.lower()])}


