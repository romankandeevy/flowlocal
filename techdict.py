# Готовые подстановки-заготовки: «моя почта», «подпись» и прочее частое.
#
# Родня техтерминам выше, а не отдельная затея, и потому живут в одном файле:
# и то и другое - готовый набор, который по одному нажатию падает в те же
# config.snippets, что и подстановки человека (см. settings_qt.setSnippetPreset).
# Механику копируем у setTechTerms, второго хранилища не заводим.
#
# Разница с терминами - в наполнении. Термин знает, что на что менять
# («джейсон» -> JSON). Заготовка задаёт только ЛЕВУЮ часть - фразу, - а правую
# («put») человек вписывает сам: свою почту, телефон, реквизиты у всех разные,
# угадать их нельзя. Поэтому у пятёрки «впиши сам» put пустой.
#
# Часть заготовок идёт с ГОТОВЫМ значением через переменные cleaner._fill_vars
# ({дата}, {дата-словами}, {время}, {день-недели}) - их вписывать не надо, они
# сами подставят дату или время в момент вставки. Фразы для них выбраны так,
# чтобы не совпасть с голосовыми командами cleaner (_CMD_DATE «сегодняшняя
# дата», _CMD_TIME «текущее время»), иначе одно и то же делалось бы дважды.
#
# id - как у готовых преобразований (transforms.PRESETS): включённые заготовки
# помечаются им в config.snippets_presets_on, и по этому списку блок «Готовые»
# знает, что показать включённым, а таблица «Подстановки» - какие фразы не
# дублировать. Список - данными, как TERMS: держать его разметкой в QML значило
# бы прятать набор в файле вёрстки, где его не видят ни тесты, ни код.
