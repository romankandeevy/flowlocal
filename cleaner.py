"""Постобработка текста - аналог "AI-полировки" Wispr Flow.

Первый слой - словарь (config.dictionary): чинит свои термины и фамилии.
Базовый слой - междометия и слова-паразиты. Паразитов ищем не по списку слов, а
по запятым вокруг них: GigaAM ставит запятую там, где говорящий запнулся, - см.
_PARASITES ниже. Опциональный слой - локальная LLM через Ollama
(config.llm.enabled): берёт то, что запятыми не берётся, - самоисправления и
тон. Коробочную LLM пробовали и отвергли замером: PLAN 2.6-бис.
Последний слой - сниппеты (config.snippets): подстановка "сказать -> вставить"
для адресов и реквизитов, которые надоело диктовать по буквам.
При любой ошибке любого слоя возвращается исходный текст - диктовка не должна
ломаться из-за недоступной Ollama или битого конфига.
"""

import functools
import json
import re
import time
import urllib.request

# Только однозначные междометия. Кириллица и латиница - разные кодпоинты,
# поэтому английское "um" не зацепит русское слово "ум".
_FILLERS = re.compile(
    r"(?i)(?:^|(?<=[\s,;:]))"
    r"(?:э[-э]+м*|эм+|м{3,}|uh+m*|um+|hm{2,})"
    r"(?=[\s,.!?;:…-]|$)",
)

# --------------------------------------------------------------------------
# Слова-паразиты: паразита выдаёт не слово, а запятые вокруг него.
#
# «Вот» вырезать нельзя - «вот дом» обычная фраза. «Короче» нельзя - «короче
# путь». «В общем» нельзя - «в общем виде». Поэтому старый фильтр ловил одни
# «э-э» и на живой речи не делал ничего: в жалобе владельца «И вот, короче, я,
# угу» он не тронул ни слова.
#
# Ключ нашёлся в самой модели: GigaAM ставит запятую там, где говорящий
# запнулся. Значит разметка заминок уже в тексте - её надо прочитать, а не
# угадывать смысл. «И вот, короче, я» - запятые вокруг «короче» и есть след
# паузы; «Вот дом» - паузы не было, запятой нет, слово настоящее.
#
# Почему не LLM: проверено на Qwen3 (PLAN 2.6). 0.6B выкинула «Вот дом, в
# котором я живу» -> «дом», 1.7B с рассуждением потеряла «на 15%». Генератор
# по устройству может вернуть что угодно. Здесь мы ничего не генерируем -
# только вычёркиваем размеченное, и потерять текст структурно нечем.
_PARASITES = [
    "ну", "вот", "ну вот", "короче", "типа", "как бы", "в общем", "в принципе",
    "это самое", "значит", "понимаешь", "понимаете", "знаешь", "знаете",
    "слушай", "слушайте", "собственно", "так сказать", "скажем так",
    "в смысле", "угу", "ага", "э", "эм", "мм",
]
# Намеренно НЕ здесь, хотя просятся:
#   «наверное», «может быть»  - это неуверенность говорящего, а не сор;
#                               убрать - значит переписать смысл;
#   «кстати», «честно говоря» - вводят мысль, несут интонацию;
#   «просто», «то есть»       - «просто нажми», «то есть как?» - обычные слова.
# Цена ошибки несимметрична: оставить паразита - полбеды, съесть слово - потеря
# текста, а этого мы не делаем никогда.
_P_ALT = "|".join(sorted((re.escape(p) for p in _PARASITES), key=len, reverse=True))

# Между запятыми: «, короче,» -> «,»
_P_MID = re.compile(rf"(?i),\s*(?:{_P_ALT})\s*(?=[,.!?…])")
# В начале фразы или сразу после союза: «Ну вот, убирай», «И вот, короче, я»
_P_HEAD = re.compile(rf"(?i)(?:(?<=^)|(?<=[.!?…]\s))((?:и|а|но|да)\s+)?"
                     rf"(?:{_P_ALT})\s*,\s*")
# Перед концом предложения: «я, угу.» -> «я.»
_P_TAIL = re.compile(rf"(?i),\s*(?:{_P_ALT})\s*(?=[.!?…]|$)")

# Слова словаря разбираем по границам слов, кириллица для \w - полноценные буквы.
_WORD = re.compile(r"\w+", re.UNICODE)

# Насколько сильно распознанное слово может отличаться от словарного, чтобы мы
# всё-таки признали в нём словарное. Лесенка по длине - не вкусовщина, а цена
# ошибки: на коротком слове одна правка - это уже другое слово («кости» ->
# «Корти»), а на длинном одна правка - почти наверняка та самая осечка
# распознавания («сокалов» -> «Соколов»). Короткие поэтому только
# капитализируем, а гадать не беремся: не поправить термин - полбеды,
# подменить верно распознанное слово - потеря текста.
_DICT_STEPS = ((8, 2), (6, 1), (4, 0))


def _levenshtein(a: str, b: str) -> int:
    prev = list(range(len(b) + 1))
    for i, ca in enumerate(a, 1):
        cur = [i]
        for j, cb in enumerate(b, 1):
            cur.append(min(prev[j] + 1, cur[j - 1] + 1, prev[j - 1] + (ca != cb)))
        prev = cur
    return prev[-1]


def _dict_match(chunk: str, entry: str) -> bool:
    """Признать ли в chunk слово entry из словаря."""
    a = chunk.lower().replace("ё", "е")
    b = entry.lower().replace("ё", "е")
    if a == b:
        return chunk != entry              # тот же термин, поправить только регистр
    # Русский язык склоняет: «Соколовым» при словарном «Соколов» распознано
    # ВЕРНО, и замена на именительный падеж сломала бы фразу. Словарное слово
    # целиком в начале - это словоформа, а не осечка. Не трогаем.
    if a.startswith(b):
        return False
    limit = next((lim for ln, lim in _DICT_STEPS if len(b) >= ln), 0)
    if not limit or abs(len(a) - len(b)) > limit:
        return False
    return _levenshtein(a, b) <= limit


def _apply_dictionary(text: str, words) -> str:
    """Словарь: «сказал термин - получил термин, как записано в настройках».

    В faster-whisper это делалось параметром hotwords - словарь подмешивался в
    декодер и влиял на само распознавание. У onnx-asr такого параметра нет и
    быть не может: декодер RNN-T не авторегрессивный, подсказывать ему нечем
    (см. transcriber.py). Поэтому чиним уже готовый текст.

    Правим по окну в столько слов, сколько их в словарной записи, - запись
    может быть и из нескольких слов. Длинные записи идут первыми: иначе
    короткая перехватила бы совпадение раньше длинной и обрезала бы её.

    Чего словарь не умеет и уметь не должен: переводить «гигаам» в «GigaAM».
    Кириллица и латиница - разные кодпоинты, для меры расстояния это не осечка
    в один символ, а другое слово целиком. Для таких замен есть сниппеты: там
    сказано прямо, что на что менять, и гадать не приходится.
    """
    entries = sorted({w.strip() for w in (words or [])
                      if isinstance(w, str) and len(w.strip()) >= _DICT_STEPS[-1][0]},
                     key=len, reverse=True)
    if not entries:
        return text
    toks = list(_WORD.finditer(text))
    out: list[str] = []
    pos = i = 0
    while i < len(toks):
        for e in entries:
            n = len(e.split())
            if i + n > len(toks):
                continue
            start, end = toks[i].start(), toks[i + n - 1].end()
            if _dict_match(text[start:end], e):
                out.append(text[pos:start])
                out.append(e)
                pos = end
                i += n
                break
        else:
            i += 1
    out.append(text[pos:])
    return "".join(out)


