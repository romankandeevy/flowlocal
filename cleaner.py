def _strip_fillers(text: str) -> str:
    original = text
    started_upper = bool(text[:1].isupper())
    text = _FILLERS.sub("", text)

    # Паразиты, размеченные запятыми. Крутим до сходимости: «Ну вот, короче,»
    # - два подряд, и первый проход снимает только внешний.
    prev = None
    while prev != text:
        prev = text
        text = _P_HEAD.sub(lambda m: m.group(1) or "", text)   # союз сохраняем
        text = _P_MID.sub("", text)
        text = _P_TAIL.sub("", text)

    text = re.sub(r"\s+([,.!?;:…])", r"\1", text)      # пробел перед знаком
    text = re.sub(r"([,;:])(\s*[,;:])+", r"\1", text)  # "раз, , вот" после вырезания
    # Точка, а следом запятая: "я, угу. Э-э, вот." -> "я, угу., вот." Правило
    # выше схлопывает только запятые подряд, а тут знак конца предложения
    # столкнулся с запятой от вырезанного филлера. Конец предложения сильнее.
    text = re.sub(r"([.!?…])\s*[,;:]+", r"\1", text)
    # Две точки подряд: «...утра. ммм, вот.» -> вырезали «вот», и осиротевшая
    # точка от него встала рядом с настоящей: «...утра. .» Первый знак главный.
    text = re.sub(r"([.!?…])\s*[.!?]+(?=\s|$)", r"\1", text)
    text = re.sub(r"^[\s,;:…-]+", "", text)            # осиротевшая пунктуация в начале
    text = re.sub(r"\s{2,}", " ", text)
    text = text.strip()
    # Заглавная после вырезанного начала - и в первом предложении, и в каждом
    # следующем: «...всё. В общем, сделай» -> «...всё. сделай» без этого.
    text = re.sub(r"([.!?…]\s+)([а-яёa-z])",
                  lambda m: m.group(1) + m.group(2).upper(), text)
    if started_upper and text[:1].islower():
        text = text[0].upper() + text[1:]

    # Предохранитель. Фраза «Э-э, ммм, вот.» состоит из паразитов целиком, и
    # чистка сводит её к «.» - то есть к пустоте. По смыслу это верно: человек
    # не сказал ничего. Но молча съесть диктовку мы не имеем права ни при каких
    # обстоятельствах - пусть лучше вставится сор, и человек сотрёт его сам.
    if not re.search(r"\w", text):
        return original
    return text


# Кого предпочесть, если у человека стоит несколько моделей. Порядок - по
# пригодности к нашей работе, а не по силе: чистка диктовки не требует ума, она
# требует послушности и скорости. 3-4B хватает (замер PLAN 2.6-бис: 1.7B уже
# слишком глупа, 8B будет думать дольше, чем человек готов ждать вставку).
_LLM_PREFER = ("qwen2.5:3b", "qwen3:4b", "qwen2.5:7b", "llama3.2:3b",
               "llama3.1:8b", "gemma2:2b", "phi3.5", "mistral")


def probe_ollama(url: str, timeout: float = 1.5) -> list[str]:
    """Какие модели у локальной Ollama. Пустой список - её нет.

    Таймаут маленький: это проверка на старте, и ждать её человек не должен.
    Не отвечает за полторы секунды - считаем, что нет, и работаем без неё.
    """
    try:
        req = urllib.request.Request(url.rstrip("/") + "/api/tags")
        with urllib.request.urlopen(req, timeout=timeout) as resp:
            data = json.loads(resp.read().decode("utf-8"))
        return [str(m.get("name", "")) for m in data.get("models", []) if m.get("name")]
    except Exception:  # noqa: BLE001 - нет так нет, это не ошибка
        return []


def pick_llm_model(available: list[str], want: str = "") -> str:
    """Какую из установленных моделей взять. Пусто - брать нечего.

    Сначала то, что уже выбрано в конфиге: человек мог поставить осознанно.
    Потом наш список предпочтений. Потом - хоть что-нибудь, кроме моделей,
    которые для текста не годятся: embed-модели вернут вектор вместо ответа,
    а vision-модели ждут картинку.
    """
    if not available:
        return ""
    if want and any(a == want or a.startswith(want + ":") for a in available):
        return want
    for p in _LLM_PREFER:
        for a in available:
            if a == p or a.startswith(p.split(":")[0] + ":"):
                return a
    # Список запретов заведомо неполон - имена моделей никто не стандартизовал,
    # и завтра выйдет очередная. Это не страшно: если сюда всё-таки просочится
    # что-то, отвечающее вектором или описанием картинки, ответ не пройдёт
    # _sane() и человек получит сырой текст. Здесь мы экономим лишний запрос,
    # а не защищаемся - защита ниже по конвейеру.
    for a in available:
        low = a.lower()
        if not any(bad in low for bad in ("embed", "vision", "-vl", "clip", "rerank",
                                          "llava", "moondream", "minicpm-v", "bakllava")):
            return a
    return ""


def autoconfig_llm(cfg: dict, log) -> str:
    """Нашли Ollama - включили полировку. Не нашли - работаем без неё.

    Возвращает имя выбранной модели или пустую строку. Конфиг правит на месте.

    Трогаем только llm.enabled = None («человек не решал»). Если он поставил
    true или false руками - это его решение, и переигрывать его мы не будем:
    самое противное в программах - когда они сами себе включают то, что ты
    выключил.

    Зовётся из потока, не из главного: probe_ollama ходит по сети, и полторы
    секунды ожидания на старте - это полторы секунды, когда хоткей не работает.
    """
    llm = cfg.get("llm")
    if not isinstance(llm, dict):
        return ""

    # Своя модель в models/ - она никуда не делась и ничего не спрашивает.
    # Проверяем первой: если человек её скачал, то он этот выбор уже сделал, и
    # искать чужой сервер незачем.
    if str(llm.get("backend") or "") == "local":
        import llm_local

        name = str(llm.get("local_model") or "")
        if not llm_local.available(name):
            if llm.get("enabled"):
                log(f"llm local: модели {name} нет - чистим запятыми")
            return ""
        if llm.get("enabled") is None:
            llm["enabled"] = True
        t0 = time.monotonic()
        if _ask("ок", {**llm, "timeout_sec": 120}, log, "warmup"):
            log(f"llm прогрета за {time.monotonic() - t0:.1f} с")
        return name

    have = probe_ollama(str(llm.get("url") or ""))
    if not have:
        if llm.get("enabled"):
            log("ollama не отвечает - полировка не сработает, чистим запятыми")
        return ""

    model = pick_llm_model(have, str(llm.get("model") or ""))
    if not model:
        log(f"ollama есть, но подходящей модели нет: {', '.join(have)}")
        return ""

    llm["model"] = model
    if llm.get("enabled") is None:
        llm["enabled"] = True
        log(f"нашли ollama, полировка включена: {model}")

    # Прогрев. Первый вызов к холодной Ollama - это ~30 с загрузки модели в
    # память: дольше таймаута, и первая же диктовка осталась бы без полировки
    # (замер: «timed out» на первом вызове, 0.3 с на последующих). Мы и так в
    # фоновом потоке - греем здесь, чтобы к первой фразе модель уже сидела в
    # памяти. keep_alive в запросе держит её там дальше.
    t0 = time.monotonic()
    if _ask("ок", {**llm, "timeout_sec": 120}, log, "warmup"):
        log(f"llm прогрета за {time.monotonic() - t0:.1f} с")
    return model


def _ask(prompt: str, llm_cfg: dict, log, what: str) -> str:
    """Один запрос к LLM, какой бы она ни была. Пустая строка - не получилось.

    Два движка, один контракт:

        ollama  чужая программа с сервером на порту. Работает отлично, но
                поставить и настроить её человеку, который пишет письма, никто
                не поможет;
        local   onnxruntime-genai в нашем же процессе. Рантайм уже в сборке
                ради распознавания, модель качается в models/ рядом с GigaAM.

    Выбор - в конфиге (`llm.backend`). Умолчание - ollama: у тех, у кого она
    уже стоит, ничего не должно измениться от этой правки.
    """
    if str(llm_cfg.get("backend") or "ollama") == "local":
        import llm_local
        return llm_local.ask(prompt, llm_cfg, log, what)
    return _ollama(prompt, llm_cfg, log, what)


def _ollama(prompt: str, llm_cfg: dict, log, what: str) -> str:
    """Один запрос к локальной Ollama. Пустая строка - не получилось.

    Всё, включая сборку запроса, внутри try: раньше Request() стоял снаружи,
    и пустой/битый url в конфиге (KeyError, ValueError) убивал clean() целиком -
    то есть терялась уже распознанная фраза. LLM опциональна, диктовка - нет.
    """
    try:
        req = urllib.request.Request(
            llm_cfg["url"].rstrip("/") + "/api/generate",
            data=json.dumps(
                {
                    "model": llm_cfg["model"],
                    "prompt": prompt,
                    "stream": False,
                    "options": {"temperature": 0.1},
                    # Умолчание Ollama - выгрузить модель после 5 минут покоя.
                    # Для диктовки это значит «почти каждый вызов холодный»:
                    # загрузка ~30 с, наш таймаут меньше, полировка молча не
                    # срабатывает. Держим модель час с последнего вызова.
                    "keep_alive": "60m",
                }
            ).encode("utf-8"),
            headers={"Content-Type": "application/json"},
        )
        with urllib.request.urlopen(req, timeout=llm_cfg.get("timeout_sec", 20)) as resp:
            return json.loads(resp.read().decode("utf-8")).get("response", "").strip()
    except Exception as e:  # noqa: BLE001 - LLM опциональна, диктовка важнее
        log(f"ollama {what} failed: {e}")
        return ""


def _norm_words(text: str) -> list[str]:
    """Слова без пунктуации и регистра - для сравнения «те же ли это слова»."""
    return [w.lower().replace("ё", "е") for w in _WORD.findall(text)]


def _is_deletion_of(before: str, after: str) -> bool:
    """Является ли after исходником с вычеркнутыми словами.

    Железное свойство полировки: она ТОЛЬКО вычёркивает. Убрать паразита -
    вычёркивание. Разрешить самоисправление («в пятницу, нет, в субботу» ->
    «в субботу») - тоже вычёркивание. Пунктуацию и регистр не сравниваем,
    их модели править можно.

    Значит слова ответа обязаны быть подпоследовательностью слов исходника.
    Появилось слово, которого не было, - модель дописала или подменила, и это
    брак по построению, что бы она ни имела в виду.

    Не гипотеза, а замер: qwen2.5:3b через Ollama вставила английское «now» в
    середину русской фразы и превратила «Пришли Соколову 3 отчёта» в «Пришло
    Соколовой три отчёта» - фамилия, глагол и цифра, всё мимо. По длине эти
    ответы прошли бы: испорчены сами слова, а не размер.
    """
    src = _norm_words(before)
    i = 0
    for w in _norm_words(after):
        while i < len(src) and src[i] != w:
            i += 1
        if i == len(src):
            return False
        i += 1
    return True


def _sane(before: str, after: str, log, what: str, floor: float = 0.5) -> bool:
    """Не слишком ли вольно LLM обошлась с текстом.

    Не вкусовщина, а вывод из замера (PLAN 2.6-бис). Qwen3-0.6B на «Вот дом, в
    котором я живу» вернула «дом»; 1.7B на «Выручка выросла на 15% за квартал»
    вернула «Выручка выросла за квартал». Генератор по устройству может вернуть
    что угодно - хоть пересказ, хоть пустоту, хоть ответ на вопрос из текста.
    Полагаться на то, что модель послушная, нельзя: послушность не гарантия, а
    надежда.

    Меряем длиной в словах. Полировка убирает сор - текст обязан похудеть, но не
    вдвое: диктовка из одних паразитов не бывает. Раздулся - модель дописала от
    себя, тоже брак.

    Порог для тона мягче: перефразировать - его работа.
    """
    nb, na = len(before.split()), len(after.split())
    if not na:
        log(f"llm {what}: пустой ответ, отдаём сырой текст")
        return False
    if na < nb * floor:
        log(f"llm {what}: съедено {nb - na} слов из {nb}, отдаём сырой текст")
        return False
    if na > nb * 1.6 + 5:
        log(f"llm {what}: дописано {na - nb} слов, отдаём сырой текст")
        return False
    return True


def _llm_polish(text: str, llm_cfg: dict, log) -> str:
    got = _ask(_LLM_PROMPT.format(text=text), llm_cfg, log, "polish")
    if not _sane(text, got, log, "polish"):
        return text
    # Полировка - это вычёркивание, и ответ обязан быть исходником с
    # вычеркнутыми словами. Модель, которая подменила слово или дописала своё,
    # отвергается целиком: не наша работа - разбираться, что она имела в виду.
    if not _is_deletion_of(text, got):
        log("llm polish: в ответе слова не из исходника, отдаём сырой текст")
        return text
    return got


def llm_command(text: str, instruction: str, llm_cfg: dict, log) -> str:
    """Command mode: выполнить над выделенным текстом сказанное голосом.

    Пусто - не получилось, и вызвавший обязан НИЧЕГО не вставлять: выделение
    останется как было. Это важнее, чем кажется: мы вставляем поверх выделения,
    то есть стираем его. Вставить сюда мусор - значит уничтожить кусок чужого
    документа, а Ctrl+Z у каждого приложения свой, и вернуть мы не поможем.

    ЧЕГО ЗДЕСЬ НЕТ И БЫТЬ НЕ МОЖЕТ - проверки результата. У диктовки полировка
    только вычёркивает, и это проверяется (_is_deletion_of). Здесь правка по
    построению любая: «переведи на английский» не оставит от исходных слов
    ничего, «распиши подробнее» удлинит фразу вдесятеро. Ни длина, ни слова
    ответа ни о чём не говорят.

    Пробовал ловить болтливую модель по длине - не работает: пояснения к правке
    и честное «распиши подробнее» дают одинаковый прирост, тест это сразу и
    показал. Поэтому от болтовни защищает только промпт, а он - надежда, а не
    гарантия. Так у всех, кто делает эту функцию, и врать про это не будем.

    Остаётся честная граница: ответ не пуст (иначе нечего вставлять) и не
    абсурден по размеру - это не гарантия качества, а страховка от вставки
    романа в чужой документ.
    """
    if not text.strip() or not instruction.strip():
        return ""
    got = _ask(_COMMAND_PROMPT.format(text=text, instruction=instruction),
                  llm_cfg, log, "command")
    if not got:
        return ""
    if len(got) > _COMMAND_MAX_CHARS:
        log(f"llm command: ответ {len(got)} символов - это уже не правка, "
            f"не вставляем")
        return ""
    return got


def _llm_tone(text: str, tone: str, llm_cfg: dict, log) -> str:
    got = _ask(_TONE_PROMPT.format(text=text, tone=tone), llm_cfg, log, "tone")
    # Тону позволено больше: он для того и зван, чтобы переписать фразу.
    return got if _sane(text, got, log, "tone", floor=0.4) else text


def tone_for(process: str, cfg: dict) -> str:
    """Тон, заданный для этого приложения. Пусто - правила нет, не трогаем.

    Ключ - имя exe активного окна ('outlook.exe'), значение - тон словами
    ('формально'), он же уходит в промпт как есть. Словами, а не списком из
    двух вариантов: LLM всё равно читает это как текст, а человеку не надо
    подбирать формулировку под наш enum.
    """
    rules = cfg.get("tone_rules")
    if not process or not isinstance(rules, dict):
        return ""
    return str(rules.get(process.lower()) or "")


@functools.lru_cache(maxsize=1)
def _compile_snippets(items: tuple) -> tuple:
    """Собирает ОДИН regex-alternation на все сниппеты сразу.

    Почему не N отдельных re.sub подряд: значение одного сниппета может само
    содержать ключ другого (в задаче это буквально показано на примере ключа
    "почта" и значения "ПОЧТА"). Если гонять re.sub по кругу, после подстановки
    первого сниппета в тексте появляется чужой ключ, и следующий re.sub в
    цепочке заменит уже подставленное значение - самоподмена. Pattern.sub()
    ищет совпадения только в ИСХОДНОЙ строке за один проход и не пересматривает
    то, что сам только что вставил, поэтому один общий вызов от такой цепочки
    застрахован.

    Длинные ключи в alternation идут первыми: re перебирает варианты слева
    направо и берёт первый подошедший, а не самый длинный (это не POSIX-regex) -
    без сортировки по длине короткий ключ ("почта") перехватил бы совпадение
    раньше длинного ("моя почта") и обрезал бы его.

    items - уже отсортированный tuple(dict.items()) из _apply_snippets: dict
    нельзя ни хранить в качестве ключа кэша, ни передать в lru_cache напрямую
    (нехешируем), а clean() дёргается на каждую диктовку - пересобирать этот
    regex заново на каждую фразу незачем, если сниппеты в настройках не менялись.
    """
    # Пустой ключ - отдельная ловушка: пустая строка совпадает нулевой длины
    # на каждой границе слова, и такой сниппет распылил бы своё значение по
    # всему тексту. Опечатка в конфиге не должна съедать распознанную фразу.
    keys_by_len = sorted((k for k, _ in items if k), key=len, reverse=True)
    pattern = re.compile(
        r"\b(?:" + "|".join(re.escape(k) for k in keys_by_len) + r")\b",
        re.IGNORECASE,
    )
    by_lower = {k.lower(): v for k, v in items}
    return pattern, by_lower


def _apply_snippets(text: str, snippets: dict) -> str:
    """Подстановка "сказать -> вставить" (PLAN 4.1: "моя почта" -> адрес).

    Сравнение без учёта регистра и по границам слов - в Python и \\b, и
    re.IGNORECASE юникодные по умолчанию для обычных строк, кириллица для них
    полноценные "буквы слова". Поэтому короткий ключ "почта" не сработает
    внутри "почтальонша": сразу после совпадения там идёт буква "л", а не
    граница слова. Ключ может быть из нескольких слов и содержать
    regex-метасимволы - экранируется в _compile_snippets через re.escape().
    Регистр ЗНАЧЕНИЯ не трогаем: это адрес или реквизиты из конфига как есть,
    капитализировать их нельзя.
    """
    if not snippets or not isinstance(snippets, dict):
        return text
    items = tuple(sorted((k, v) for k, v in snippets.items() if k))
    if not items:
        return text
    pattern, by_lower = _compile_snippets(items)
    return pattern.sub(lambda m: by_lower[m.group(0).lower()], text)


# --- Голосовые команды (аналог dictation commands Wispr Flow) ---------------
#
# Ровно три команды, и все три - про то, чего голосом иначе не сделать:
# перевод строки, абзац и Enter в конце (отправка сообщения в чатах).
# Пунктуацию командами не делаем - GigaAM ставит её сам.
#
# Список нарочно короткий и фразы нарочно "канцелярские": цена ложного
# срабатывания - испорченный текст, а надиктовать "с новой строки" в обычной
# речи почти невозможно. Модель может обернуть команду пунктуацией и заглавной
# («. С новой строки, ») - поэтому вокруг фразы съедаются знаки и пробелы.

# Только формы с предлогом: «новая строка» и «новый абзац» без него - обычная
# речь («новая строка кода начинается с отступа»), и команда из них стирала бы
# настоящий текст - проверено тестом-ловушкой.
#
# Перед фразой съедается только запятая («привет, с новой строки») - точка
# перед ней принадлежит предыдущему предложению и должна остаться. После -
# любой знак: он артефакт самой команды («С новой строки.»).
_CMD_NEWLINE = re.compile(
    r"[ \t]*,?[ \t]*\b(?:с\s+новой\s+строки|перенос\s+строки)\b[ \t]*[,.;:]?[ \t]*",
    re.IGNORECASE)
_CMD_PARAGRAPH = re.compile(
    r"[ \t]*,?[ \t]*\bс\s+нового\s+абзаца\b[ \t]*[,.;:]?[ \t]*",
    re.IGNORECASE)
# Только в САМОМ конце фразы: «нажми энтер» посреди текста - это цитата или
# инструкция кому-то, а не команда нам.
_CMD_ENTER = re.compile(
    r"[\s,.;:!?…]*\b(?:нажми|нажать)\s+(?:энтер|интер|enter)\b[\s.!]*$",
    re.IGNORECASE)


def extract_commands(text: str, cfg: dict) -> tuple[str, bool]:
    """(текст с применёнными командами, нажать ли Enter после вставки).

    Зовётся ПОСЛЕ clean(): полировка LLM не должна видеть команды - перепишет
    или послушается, оба исхода хуже. И никогда не бросает - как и clean():
    исключение здесь потеряло бы уже распознанную фразу.
    """
    try:
        if not text or not cfg.get("voice_commands", True):
            return text, False
        press_enter = False
        m = _CMD_ENTER.search(text)
        if m and m.start() > 0:          # фраза не может состоять из одной команды
            text = text[:m.start()]
            press_enter = True
        text = _CMD_PARAGRAPH.sub("\n\n", text)
        text = _CMD_NEWLINE.sub("\n", text)
        # После перевода строки модельная «строчная после запятой» смотрится
        # ошибкой: новая строка читается как новое предложение.
        text = re.sub(r"\n([а-яёa-z])", lambda m: "\n" + m.group(1).upper(), text)
        return text.strip(), press_enter
    except Exception:  # noqa: BLE001
        return text, False


def clean(text: str, cfg: dict, log, process: str = "") -> str:
    """Никогда не бросает: на любой ошибке возвращает то, что пришло.

    Вызывается между распознаванием и вставкой, и исключение здесь означало бы
    молча проглоченную фразу - худшее, что может сделать диктовка.

    process - имя exe окна, куда поедет текст ('outlook.exe'): по нему
    подбирается тон. Пусто - тон не трогаем.
    """
    try:
        # Словарь - первым: он чинит осечки распознавания, а все слои ниже
        # (и промпты LLM, и сниппеты) рассчитывают на верные слова.
        text = _apply_dictionary(text, cfg.get("dictionary"))
        if cfg.get("remove_fillers", True):
            text = _strip_fillers(text)
        llm = cfg.get("llm") or {}
        if text and llm.get("enabled"):
            text = _llm_polish(text, llm, log)
        # Сниппеты - после полировки, но ДО тона. После полировки: её промпт
        # видит только фразу-триггер («моя почта»), а не подставленный адрес, и
        # переписать его не может. До тона: тон переписывает текст целиком и
        # запросто перефразировал бы сам триггер («моя почта» -> «мой адрес»),
        # и тогда подстановка уже не сработала бы. Подставляем раньше - тону
        # достаётся готовый адрес, а его промпт велит оставлять адреса дословно.
        text = _apply_snippets(text, cfg.get("snippets"))
        # Тон - второй проход той же LLM, и только вместе с полировкой. Не
        # потому, что иначе нельзя, а потому, что переписывать нечем: тон делает
        # Ollama. Дёргать её ради одного тона, когда полировку выключили,
        # значило бы тайком включить выключенное - и добавить ещё секунды к
        # задержке, которая сейчас вся ~0.7 с.
        if text and llm.get("enabled"):
            tone = tone_for(process, cfg)
            if tone:
                text = _llm_tone(text, tone, llm, log)
    except Exception as e:  # noqa: BLE001
        log(f"clean failed: {e}")
    return text
