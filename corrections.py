"""Самоисправления на ходу: «в пятницу, нет, в субботу» -> «в субботу».

Диктуя, человек поправляет себя вслух, и распознавание честно записывает обе
версии. Раньше это чинила только Ollama - последняя её обязательная работа в
конвейере. Здесь то же самое сделано правилами: без модели, без сети, за доли
миллисекунды и без риска, что генератор допишет своё (чем это кончалось -
записано в cleaner._sane и cleaner._is_deletion_of).

Мы НЕ понимаем текст и не пытаемся. Опора одна - параллельность: поправка
повторяет конструкцию, которую исправляет. «в пятницу» и «в субботу» - один
предлог; «Маше» и «Кате» - одно окончание. Нет параллели - не трогаем ничего.
Цена ошибки несимметрична: пропущенная поправка это полбеды, съеденное слово -
потеря текста, а этого мы не делаем никогда.

Самоисправления без явного маркера («в пятницу в субботу») здесь не ловятся и
не будут: это генеративная задача, правилам она не по силам (PLAN, задача 3).

Зовётся из cleaner.clean() ПОСЛЕ расстановки знаков: половина маркеров
опознаётся по запятым вокруг них, а до пунктуации запятых в тексте нет.
"""

import re

# Маркеры поправки. Список закрытый и короткий. Каждое слово в нём бывает и
# обычным («нет времени», «ой как хорошо»), поэтому сам маркер ничего не решает:
# он только показывает место, где стоит проверить параллельность.
_MARKERS = frozenset(("нет", "ой", "точнее", "вернее"))

# «то есть не» разбирается отдельно, потому что устроено наоборот: после него
# идёт ОТВЕРГНУТЫЙ вариант, а верный - следующим куском («в пятницу, то есть не
# в пятницу, а в субботу»). Считать верным кусок сразу за маркером, как у
# остальных, значило бы выкинуть ровно то слово, которое человек оставлял.
_MARKER_NOT = ("то", "есть", "не")

_TOKEN = re.compile(r"\w+(?:-\w+)*", re.UNICODE)

# Что обрывает кусок. Мягкие знаки отделяют кусок внутри фразы, точка и её
# родня - целое предложение: через границу предложений поправок не бывает.
_SOFT = ",;:"
_ENDS = ".!?…"
_BREAK = _SOFT + _ENDS + "\n"

_VOWELS = "аеиоуыэюя"

# Предлоги для признака «одинаковое первое слово». Только предлоги: совпади
# здесь любое слово, и «я думаю, нет, я уверен» стало бы поправкой, хотя это
# две разные мысли.
_PREPS = frozenset((
    "в", "во", "на", "о", "об", "обо", "с", "со", "к", "ко", "из", "изо",
    "от", "до", "у", "за", "под", "над", "про", "для", "без", "через",
    "между", "перед", "по", "при",
))

# Кусок длиннее четырёх слов поправкой почти никогда не бывает: длинное «нет» -
# это обычно отрицание, а не оговорка.
_MAX_WORDS = 4

# Поправок в одной фразе бывает несколько («Маше, нет, Кате, в пятницу, нет, в
# субботу»). Каждый проход строго укорачивает текст, так что цикл конечен и без
# счётчика, но счётчик дешевле веры в это.
_MAX_PASSES = 3


def _norm(word: str) -> str:
    return word.lower().replace("ё", "е")


def _words(chunk) -> list[str]:
    return [t[2] for t in chunk]


def _same_tail(a: str, b: str) -> bool:
    """Похожи ли хвосты слов - грубая проверка, тот ли это падеж.

    План просит «совпадение последних 2+ букв», но обязательный случай
    «Маше | ой | Кате» этой мерке не отвечает: общая у них ровно одна буква.
    Русское окончание часто одной гласной и исчерпывается - «МашЕ» и «КатЕ»,
    «пятницУ» и «субботУ», - поэтому одну совпавшую букву засчитываем, если она
    гласная. Согласная в конце («был» и «стол») - обычно часть корня, а не
    падеж, и одной её мало.
    """
    n = 0
    while n < len(a) and n < len(b) and a[-1 - n] == b[-1 - n]:
        n += 1
    if n >= 2:
        return True
    return n == 1 and a[-1] in _VOWELS


def _parallel(left: list[str], right: list[str], *,
              marked: bool, repeat_ok: bool = False) -> int:
    """Сколько слов с конца левого куска параллельны правому. 0 - параллели нет.

    Сравниваем не куски целиком, а хвост левого с головой правого. Причина в
    критерии готовности из плана: «встреча в пятницу нет в субботу в десять»
    идёт без единой запятой, кусками там будут «встреча в пятницу» и «в субботу
    в десять», и целиком они не параллельны - параллельна пара «в пятницу | в
    субботу» внутри них. Поправка заменяет составляющую, а не всё, что человек
    успел сказать.

    marked - маркер отбит запятой. Без запятой засчитываем только признак
    предлога: одного созвучия окончаний мало, иначе обычное «У книги нет
    обложки» превратилось бы в «У обложки» - слово потеряно на ровном месте.
    """
    lw = [_norm(w) for w in left]
    rw = [_norm(w) for w in right]

    # Признак 1: обе стороны начинаются одним и тем же предлогом.
    # k наименьшее: выкинуть лишнее слово дороже, чем недовыкинуть. «в пять в
    # пятницу, нет, в субботу» - поправка про пятницу, а не про всё время.
    for k in range(1, min(len(lw), len(rw)) + 1):
        if lw[-k] == rw[0] and rw[0] in _PREPS and (repeat_ok or lw[-k:] != rw[:k]):
            return k

    # Признак 2: столько же слов и похожее окончание последнего.
    # Правый кусок берём целиком, а слева ровно столько же слов с конца. Если
    # справа слов больше, чем слева, - равнять нечего и мы молчим: так «Воды
    # нет, еды нет» остаётся нетронутым.
    if marked and len(rw) <= len(lw):
        k = len(rw)
        if (repeat_ok or lw[-k:] != rw) and _same_tail(lw[-1], rw[-1]):
            return k
    return 0


def _marker_at(toks, i: int) -> int:
    """Индекс за маркером, если маркер начинается на i. 0 - маркера здесь нет."""
    if (i + 3 <= len(toks)
            and [_norm(t[2]) for t in toks[i:i + 3]] == list(_MARKER_NOT)):
        return i + 3
    return i + 1 if _norm(toks[i][2]) in _MARKERS else 0


def _left_of(text: str, toks, i: int, marker_start: int):
    """Кусок слева: от предыдущего знака (или начала фразы) до маркера.

    Запятая, приклеенная к маркеру, границей куска не считается - она его
    собственная. Иначе у «в пятницу, нет, в субботу» слева не осталось бы ничего.
    """
    lead = text[:marker_start].rstrip()
    marked = bool(lead) and lead[-1] in _SOFT
    if marked:
        lead = lead[:-1]
    p = max(lead.rfind(c) for c in _BREAK)
    return [t for t in toks[:i] if t[0] > p], marked


def _right_of(text: str, toks, j: int, start: int):
    """Кусок справа: от start до ближайшего знака. -> (слова, позиция знака)."""
    stops = [p for p in (text.find(c, start) for c in _BREAK) if p >= 0]
    q = min(stops) if stops else len(text)
    return [t for t in toks[j:] if t[0] >= start and t[1] <= q], q


def _try(text: str, toks, i: int, j: int):
    """Сработала ли поправка на этом маркере. -> (откуда резать, докуда) или None."""
    ms, me = toks[i][0], toks[j - 1][1]
    left, marked = _left_of(text, toks, i, ms)
    if not left or len(left) > _MAX_WORDS:
        return None

    after = re.match(r"\s*[,;:]", text[me:])
    start = me + after.end() if after else me
    marked = marked or bool(after)
    right, q = _right_of(text, toks, j, start)
    if not right or len(right) > _MAX_WORDS:
        return None

    if j - i == 1:                       # обычный маркер: верное - справа
        k = _parallel(_words(left), _words(right), marked=marked)
        return (left[-k][0], right[0][0]) if k else None

    # «то есть не»: справа от маркера повтор ошибочного, верное - за следующей
    # запятой. Нет третьего куска - значит это не оговорка, а обычное отрицание
    # («позвони Маше, то есть не Кате»), и трогать его нельзя.
    if q >= len(text) or text[q] not in _SOFT:
        return None
    third, _ = _right_of(text, toks, j, q + 1)
    if len(third) > 1 and _norm(third[0][2]) in ("а", "но"):
        third = third[1:]                # «а» вводит верный вариант, оно лишнее
    if not third:
        return None
    # Повтор здесь не запрещён, а наоборот ожидаем: человек буквально повторяет
    # то, от чего отказывается.
    k = _parallel(_words(left), _words(right), marked=True, repeat_ok=True)
    return (left[-k][0], third[0][0]) if k else None


def _once(text: str):
    """Первая сработавшая поправка в тексте. -> (откуда, докуда) или None."""
    toks = [(m.start(), m.end(), m.group()) for m in _TOKEN.finditer(text)]
    i = 0
    while i < len(toks):
        j = _marker_at(toks, i)
        if not j:
            i += 1
            continue
        cut = _try(text, toks, i, j)
        if cut:
            return cut
        i = j
    return None


def _apply(text: str) -> str:
    if not text or not text.strip():
        return text
    for _ in range(_MAX_PASSES):
        cut = _once(text)
        if not cut:
            break
        a, b = cut
        out = text[:a] + text[b:]
        head = text[:a].rstrip()
        # Начало предложения уехало вместе с выкинутым куском - вернуть
        # заглавную: «В пятницу, нет, в субботу.» -> «В субботу.»
        if ((not head or head[-1] in _ENDS)
                and text[a].isupper() and out[a:a + 1].islower()):
            out = out[:a] + out[a].upper() + out[a + 1:]
        text = out
    return text


def apply(text: str) -> str:
    """Убрать самоисправления. Слова только вычёркиваются, новых не появляется.

    Не бросает никогда: зовётся из clean() между распознаванием и вставкой, и
    исключение здесь означало бы молча проглоченную фразу.
    """
    try:
        return _apply(text)
    except Exception:  # noqa: BLE001 - поправка не стоит потерянной диктовки
        return text
