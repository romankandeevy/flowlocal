"""Самоисправления правилами: «в пятницу, нет, в субботу» -> «в субботу».

    $env:PYTHONIOENCODING="utf-8"
    python test_corrections.py

Ни модели, ни сети - это чистые функции над строками.

Половина случаев - ЛОВУШКИ, и они здесь главные. «Нет времени объяснять» и
«У книги нет обложки» обязаны пройти нетронутыми: маркер поправки в русском
языке чаще всего обычное слово. Цена ошибки несимметрична - пропущенная
поправка это полбеды, съеденное слово значит потерять текст, а этого мы не
делаем никогда.

Последним идёт замер на живом конвейере: критерий готовности из плана требует,
чтобы фраза чинилась С ВЫКЛЮЧЕННОЙ Ollama, то есть настоящим cleaner.clean().
"""

import sys

import cleaner
import corrections as C

fails: list[str] = []
# (что пришло, что вышло) - для общей проверки «ничего не дописано».
corpus: list[tuple[str, str]] = []


def check(cond: bool, text: str) -> None:
    print(("  ок   " if cond else "ПРОВАЛ ") + text)
    if not cond:
        fails.append(text)


def fix(src: str, want: str, name: str) -> None:
    """Поправка обязана сработать."""
    got = C.apply(src)
    corpus.append((src, got))
    check(got == want, name)
    if got != want:
        print(f"         ждали: {want!r}")
        print(f"         вышло: {got!r}")


def keep(src: str, name: str) -> None:
    """ЛОВУШКА: трогать нельзя."""
    got = C.apply(src)
    corpus.append((src, got))
    check(got == src, name)
    if got != src:
        print(f"         вышло: {got!r}")


def main() -> int:
    print("ПОПРАВКИ - маркер и параллель на месте")
    fix("в пятницу, нет, в субботу", "в субботу",
        "«в пятницу, нет, в субботу» - один предлог с обеих сторон")
    fix("Маше, ой, Кате", "Кате",
        "«Маше, ой, Кате» - одно слово и одно окончание")
    fix("Позвони Маше, вернее, Кате", "Позвони Кате",
        "«вернее» правит только последнее слово, «Позвони» остаётся")
    fix("Он придёт в пять, точнее, в шесть", "Он придёт в шесть",
        "«точнее» с предлогом: выкинут только оборот, не вся фраза")
    fix("Приходи завтра, нет, послезавтра", "Приходи послезавтра",
        "«завтра» и «послезавтра» - общий хвост длиннее двух букв")
    fix("Мы были в кино, ой, в театре", "Мы были в театре",
        "левый кусок в четыре слова - предел, но ещё берём")
    fix("Дай ручку, ой, тетрадку", "Дай тетрадку",
        "разные слова, общее окончание винительного")
    fix("Купи хлеба, в пятницу, нет, в субботу", "Купи хлеба, в субботу",
        "кусок считается от запятой, а не от начала фразы")
    fix("В пятницу, нет, в субботу.", "В субботу.",
        "начало предложения уехало - заглавная возвращается")
    fix("Приходи в пять точнее в шесть", "Приходи в шесть",
        "без запятых, но предлог общий - поправка видна и так")
    fix("Позвони Маше, нет, Кате, в пятницу, нет, в субботу",
        "Позвони Кате, в субботу",
        "две поправки в одной фразе - чиним обе")
    fix("Встреча в пятницу, то есть не в пятницу, а в субботу.",
        "Встреча в субботу.",
        "«то есть не»: повтор ошибочного и «а» уходят вместе с ним")
    fix("Я приду, то есть не приду, а позвоню.", "Я позвоню.",
        "«то есть не» с глаголом - тот же разбор")

    print("\nЛОВУШКИ - маркер есть, поправки нет")
    keep("нет времени объяснять",
         "«нет времени объяснять» - слева от маркера пусто")
    keep("Денег нет, но вы держитесь.",
         "«Денег нет, но вы держитесь» - справа слов больше, равнять нечего")
    keep("У книги нет обложки.",
         "«У книги нет обложки» - окончания созвучны, но запятой нет")
    keep("Хлеба нет, молока нет.",
         "«Хлеба нет, молока нет» - перечисление, а не оговорка")
    keep("В пятницу нет собрания.",
         "«В пятницу нет собрания» - обычное отрицание с предлогом")
    keep("Нет, я не приду.",
         "маркер в начале фразы - исправлять нечего")
    keep("Он ответил нет.",
         "маркер в конце фразы - справа пусто")
    keep("Я думаю, нет, я уверен.",
         "«я» не предлог, а окончания разные - две мысли, не поправка")
    keep("В пятницу, в субботу.",
         "предлоги параллельны, но маркера нет - не наше дело")
    keep("Мы очень долго договаривались о встрече, нет, о звонке",
         "левый кусок в шесть слов - длинное «нет» не поправка")
    keep("Позвони, ой, я забыл, как его зовут",
         "справа целая мысль, а не замена одного слова")
    keep("Позвони Маше, то есть не Кате.",
         "«то есть не» без третьего куска - это отрицание, Маша остаётся")
    keep("Нет-нет, я не про то.",
         "«нет-нет» одно слово, маркером не считается")
    keep("Нету времени.",
         "«нету» - не «нет», по границам слов не совпадает")
    keep("", "пустая строка не роняет")
    keep("   ", "одни пробелы не роняют")

    print("\nЖЕЛЕЗНОЕ СВОЙСТВО - только вычёркиваем")
    # Тот же предохранитель, которым cleaner проверяет ответ LLM: слова
    # результата обязаны быть подпоследовательностью исходных. Правила слов не
    # сочиняют по построению, но проверить дешевле, чем однажды это потерять.
    bad = [s for s, g in corpus if not cleaner._is_deletion_of(s, g)]
    check(not bad, f"во всех {len(corpus)} случаях слова только вычеркнуты")
    for s in bad:
        print(f"         появилось лишнее в: {s!r}")
    shorter = [s for s, g in corpus if len(g) > len(s)]
    check(not shorter, "результат нигде не длиннее исходника")

    print("\nЖИВОЙ КОНВЕЙЕР - критерий готовности из плана")
    # Уровень пишем явно, хотя «средне» и стоит по умолчанию: поправки теперь
    # им гейтятся, и молчаливая опора на умолчание сделала бы проверку
    # двусмысленной - непонятно, что она стережёт, гейт или конвейер.
    # Ollama выключена (llm в конфиге нет), знаки ставят правила.
    mid = {"cleanup": "medium"}
    got = cleaner.clean("встреча в пятницу нет в субботу в десять", mid,
                        lambda _m: None)
    check(got == "Встреча в субботу в десять.",
          "«средне»: «в пятницу нет в субботу» -> «Встреча в субботу в десять.»")
    if got != "Встреча в субботу в десять.":
        print(f"         вышло: {got!r}")
    check(cleaner.clean("у книги нет обложки", mid, lambda _m: None)
          == "У книги нет обложки.",
          "ЛОВУШКА на конвейере: «у книги нет обложки» цела")
    # «Слегка» обещает не трогать слова: знаки поставить, ошибку распознавания
    # по словарю поправить, а сказанное оставить как сказано. Оговорка - это
    # слова человека, и вычёркивать их на этом уровне нельзя. Та же фраза, что
    # и выше, - видно ровно то, что добавляет «средне».
    light = cleaner.clean("встреча в пятницу нет в субботу в десять",
                          {"cleanup": "light"}, lambda _m: None)
    check(light == "Встреча в пятницу нет в субботу в десять.",
          "«слегка»: оговорка на месте, добавлены только знаки")
    if light != "Встреча в пятницу нет в субботу в десять.":
        print(f"         вышло: {light!r}")
    check(cleaner.clean(None, mid, lambda _m: None) is None,
          "clean() не бросает на мусоре вместо текста")

    if fails:
        print(f"\nИТОГ: провалено {len(fails)}")
        for f in fails:
            print("  - " + f)
        return 1
    print("\nИТОГ: всё сходится")
    return 0


if __name__ == "__main__":
    sys.exit(main())
